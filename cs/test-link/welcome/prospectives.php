<h4 class="orange">What is Computer Science?</h4>

<a href="http://www.cems.uvm.edu/research/cems/snow/swe.php"><img src="gfx/charley_swe.jpg" alt="Snow water equivalent (SWE) monitoring with wireless sensor networks" width="190" height="247" border="0" class="alignright" /></a>

<p>Computer science is not just about building computers, writing computer games or creating other programs.  Computer science is the <strong>science of problem solving</strong>. The computer is just one tool that computer scientists use.</p> 

<h4 class="orange">Why Computer Science?</h4>

<p> Why should you choose computer science? Are you a creative person, interested in the world around you? Do you want to find a great job after college? Read on... </p>

<ul class="spacey">
<li><span class="larger">It's Creative</span> &#151; Apply your imagination and originality to solving problems.</li>

<li><span class="larger">It's Interdisciplinary</span> &#151; Computer science has very strong connections to many other disciplines.  Within our department alone, you'll find yourself working alongside geologists, mathematicians, engineers, psychologists, biologists and ecologists.  We believe students should be well equipped with the theory behind computer science, and be able to apply this to a problem rooted in any field. </li>

<li><span class="larger">Get a Great Job</span> &#151; In its 2008-09 handbook, the Bureau of Labor and Statistics reports that the occupation of computer software engineering is projected to grow the fastest and add the most new jobs over the 2006-16 decade. To learn about hot careers in computer science, check out:

<ul class="prospectives">
<li>ACM's <a href="http://computingcareers.acm.org/">Computing Careers and Degrees</a></li>
<li>CareerCast.com's <a href="http://www.careercast.com/jobs/content/JobsRated_10BestJobs">10 Best Jobs in America Today.</a> </li>
<li>Computerworld's <a href="http://www.computerworld.com/action/article.do?command=viewArticleBasic&amp;taxonomyName=Management&amp;articleId=330106&amp;taxonomyId=14&amp;pageNumber=1">9 Hottest Skills for '09</a></li>
<li>MSN's <a href="http://msn.careerbuilder.com/Article/MSN-1771-Salaries-and-Promotions-20-Highest-Paying-Jobs/?cbsid=634bac23c58f4730a43dcdfc061121b2-287488417-w6-6&amp;sc_extcmp=JS_1771_msn%20&amp;categoryid=sp&amp;cbRecursionCnt=2&amp;SiteId=cbmsnch41771&amp;ArticleID=1771">20 Highest-Paying Careers</a></li>
</ul>
</li>

<li><span class="larger">Impact the World</span> &#151; A degree in computer science will give you a foundation in fundamental concepts that can be applied to many problems in our world today.  Transportation, global warming, cancer research, new medical practices and human computer interaction are just a few examples of  research areas that computer scientists are actively involved in. </li>
</ul>
 

