<h5><a href="http://www.uvm.edu/~cems/cs/?Page=courses/iCompute-track.php"><img src="../../courses/gfx/icompute-composite_sm.jpg" alt="Integrative Computing (iCompute)" width="87" height="120" border="0" class="alignright" /></a>iCompute</h5>
<p>We're offering a new curriculum track &#151; <a href="http://www.uvm.edu/~cems/cs/?Page=courses/iCompute-track.php">Integrative Computing (iCompute)</a> &#151; beginning Fall 2009. Embedded, networked, and mobile systems &#151; such as iPhone and Android phones, SunSPOT and "mote" wireless sensor devices, and Lego Mindstorms robotics &#151; are just some of the platforms we'll use as a "playground" for our integrative computing innovations.
All students (CS majors or non-majors) can take this track if all the prerequisites are satisfied.</p>

<h5>Technical Talks in Computing (CS-095 and CS-195)</h5>
<p><a href="http://www.cems.uvm.edu/~cssa/"></a>
 How many of your classes offer FREE pizza at every meeting? Well, this  isn't your ordinary class. For the past <a href="http://www.cems.uvm.edu/~cssa/"><img src="http://www.uvm.edu/~cems/cs/gfx/cssa_fall08_sm.jpg" border="0" class="alignright" /></a>few years, the Computer Science Student Association (CSSA) has been arranging tech talks, geeky movie nights, <a href="http://www.cems.uvm.edu/~cssa/"></a>Linux workshops, and hacktastic project nights. The CSSA has been so successful  that the Computer Science Department had made the CSSA's informal, social, knowledge-sharing gathering a credited class.This one-credit course is open to ALL students (same course and meeting time, but different requirements) and can be taken multiple times for accumulative credit. So what are you waiting for?</p>
