<h3>Seminar Description</h3>

<h4 class="subtitle">Data Science Challenges from Petascale Astronomical Sky Surveys</h4>

<p><a href="http://classweb.gmu.edu/kborne/"><strong>Dr. Kirk Borne</strong></a><br>
 Dept of Computational & Data Sciences, George Mason University</p>

<strong>March 23, 2009</strong><br>
12:50 - 1:45 pm<br>
207 Votey Hall</p>

<p><strong>Summary</strong>
<br>
I will address the computational and data science challenges that 
will be imposed by the new petascale astronomical sky surveys 
that are coming online in the next decade.  These challenges 
include the development of efficient models and algorithms for 
several tasks in both the catalog and pixel domains.  These
tasks include:  petascale data mining for scientific knowledge
discovery, correlation mining and statistical analyses across 
hundreds of dimensions, visualization of massive data cubes, 
class discovery over a rich panoply of astrophysical parameter 
spaces, and real-time classification and characterization of 
massive collections of astronomical objects.
</p>


<p><strong>Speaker Bio</strong>
<br>
Kirk Borne is an astrophysicist at George Mason University
in the new Department of Computational and Data Sciences.
He received his PhD in astrophysics from Caltech, and
subsequently had positions at the University of Michigan,
the Carnegie Institution of Washington, the Hubble Space
Telescope Science Institute, and NASA's Goddard Space 
Flight Center.  He is currently working on the development
of one of the most impressive data-producing scientific
programs in astronomy, the Large Synoptic Survey Telescope,
which will become operational within the next 7 years.
His current research focuses on machine learning and
knowledge discovery from distributed and massive data streams,
which goes by various names:  Astro-Informatics,
Forensic Astronomy, or CSI The Cosmos.</p>

