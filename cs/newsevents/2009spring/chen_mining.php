<p><strong>Summary</strong>
<br>
The healthcare domain presents numerous opportunities for extracting
information from heterogeneous sources ranging from structured data
(e.g., laboratory results and medications) to unstructured data
(e.g., clinical documents such as discharge summaries or biomedical
documents such as Medline articles) to usage data
(e.g., audit logs that record user activity for clinical systems).
To accommodate the unique characteristics of these disparate
types of data and support the subsequent use of extracted 
information, several existing techniques can be adapted
 and applied including data mining, text mining, 
 and Web mining. This presentation will demonstrate the 
 application of each of these mining techniques in the 
 context of healthcare for: (1) studying the information needs 
 of clinicians through clinical information system usage 
 logs and subsequently enhancing relevant systems, 
 (2) acquiring disease-specific knowledge from clinical 
 and biomedical documents, and (3) enhancing patients'
 clinical problem lists.
 
 <p><strong>Speaker Bio</strong>
<br>
Elizabeth S. Chen, PhD is the Associate Director of Biomedical 
Informatics in the Center for Clinical and Translational Science and 
Assistant Professor in the Division of General Internal Medicine within the 
Department of Medicine at the University of Vermont College of Medicine. 
She received a BS in computer science from Tufts University and PhD in 
Biomedical Informatics from Columbia University. Dr. Chen's research 
primarily focuses on exploring informatics approaches for improving 
access to clinical and biomedical information. Her interests include 
building and enhancing systems (e.g., electronic health record systems, 
clinical information systems, and clinical decision support systems), 
using automated methods to study clinician information needs and evaluate 
systems, applying data mining and natural language processing techniques 
for the discovery of disease-specific knowledge, and exploring the role of 
handheld technology in patient care. Dr. Chen has previously held positions at 
Partners HealthCare System/Brigham and Women's Hospital/Harvard Medical 
School and Columbia University.
