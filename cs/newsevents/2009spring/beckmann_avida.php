<h3>Seminar Description</h3>

<h4 class="subtitle">"Evolving Energy-Conserving Distributed Behavior"</h4>

<p><a href="http://www.cse.msu.edu/~beckma24/"><strong>Benjamin Beckmann</strong></a><br>
Dept. of Computer Science and Engineering, Michigan State University</p>

<p><strong>February 2, 2009</strong><br>
12:50 - 1:45 pm<br>
207 Votey Hall</p>

<p><strong>Summary</strong>
<br>
Natural organisms are extremely well adapted to their environment. 
As software designers, we would like to build systems that are as well adapted to a virtual environment. 
Many efforts have been undertaken to produce behaviors in engineered systems that are 
reminiscent of natural organisms and/or systems.
Often, these types of systems mimic observed behaviors,
but do not benefit from the  process that produced them, specifically natural selection. 
In this talk I will present research applying digital evolution to the development of robust distributed systems.
In digital evolution, self-replicating computer programs - digital organisms - experience
mutations and selective pressures, producing complex cooperative behaviors that, like natural organisms, 
adapt to their environment.  Evolved algorithms can be translated directly into code for execution 
on computing platforms such as sensors and microrobots.</p>
<p><strong>Speaker Bio</strong>
<br>
Benjamin Beckmann is a doctoral student in the Department of Computer
Science and Engineering at Michigan State University in East Lansing, Michigan. 
As a graduate researcher in the Digital Evolution Laboratory and Software
 Engineering and Networked Systems Laboratory, Mr. Beckmann investigates
  the application of Digital Evolution to the design and application of agent-based systems. 
   Specifically, his work focuses on evolving collections of agents to perform distributed,
    energy conserving behavior.  Mr. Beckmann's work has been presented at the Genetic and
     Evolutionary Computation Conference (GECCO), the IEEE International Conference on
      Self-Adaptive and Self-Organizing Systems (SASO), and IEEE International Conference
       on Autonomic Computing (ICAC).  In addition, Mr. Beckmann's work has been featured in 
       IEEE Computer, New Scientist magazine, and World of Intelligence (a French language magazine).  
       Mr. Beckmann holds M.S. and B.S. degrees in Computer Science from Western Michigan University.
</p>

