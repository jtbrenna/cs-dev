<h3>Seminar Description</h3>

<h4 class="subtitle">"Inferring Social Networks Automatically from Sensor Data"</h4>

<p><a href="http://www.cs.dartmouth.edu/~tanzeem/"><strong>Dr. Tanzeem Choudhury</strong></a><br>
Dept. of Computer Science, Dartmouth College</p>

<strong>March 16, 2009</strong><br>
12:50 - 1:45 pm<br>
207 Votey Hall</p>

<p><strong>Summary</strong>
<br>
Human behavior in the real world is a difficult thing to study: it is
not possible to have human observers follow someone around all day, and
surveys often tend to biased and unreliable. On the other hand, sensor
data is easy to collect but inferring human behavior from this data is
still a challenging problem. In this talk, I will present the
probabilistic framework we have developed for inferring the micro-level
dynamics of human interactions as well as the macro-level latent social
network structure from local, noisy sensor observations. By studying the
micro and macro levels simultaneously we are able to explore the
relationship between interaction dynamics (local behavior) and network
prominence (a global property), and can identify the behavioral
correlates of tie strengths within a network. We believe these methods
have the potential to allow more quantitative inquiry into human
behavior and social dynamics. They will also enable us to develop
socially aware ubiquitous computing systems that are cognizant of and
responsive to users' engagement with their social environment.</p>


<p><strong>Speaker Bio</strong>
<br>
Tanzeem Choudhury is an assistant professor in the computer science
department at Dartmouth. She joined Dartmouth in 2008 after four years
at Intel Research Seattle. She received her PhD from the Media
Laboratory at MIT. Her research involves developing machine-learning
techniques for systems that can reason about human activities,
interactions, and social networks in everyday environments. Tanzeem`s
doctoral thesis demonstrated for the first time the feasibility of using
wearable sensors to capture and model social networks automatically, on
the basis of face-to-face conversations. She was recognized in MIT
Technology Review's 2008 TR35 list for her work in this area.</p>

