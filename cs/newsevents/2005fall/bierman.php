<p>
<strong>September 9, 2005</strong><br />
12:20 - 1:10 pm<br />
367 Votey Hall</p>

<p><strong>Summary</strong>
<br />
Six years ago, we had the crazy idea that students might learn better from images that touched their lives than from the abstractions offered by many textbooks.  NSF agreed and funded what seemed then to be a high-risk, proof-of-concept project to compile a web-based archive of Vermont images showing landscape change over time.</p>
<p><img class="alignright" src="../gfx/bierman09-09-05.jpg" alt="McCollough Highway in 1957: Fayston, VT, LS9773" width="300" height="175" />Since its modest beginnings, the image archive has grown dramatically and now contains over 10,000 images on line and free to all at: <a href="http://www.uvm.edu/perkins/landscape">www.uvm.edu/perkins/landscape</a>. Problem is, we’ve outgrown our simple software and with 4,000 to 6,000 hits a week, we need all the help we can get to make the website equal to the quality of images it contains.  NSF to the rescue again.  We have support over the next few years to continue improving the archive, both in terms of imagery and software.  Much of the support is dedicated to students engaged in service learning &#151; both geology and computer science students at the undergraduate and graduate levels.</p>
<p>In my talk, I’ll review the Landscape project, its website, and the science and education we have done with its images.  I’ll present some examples of what others have done with digital image archives and highlight ways in which the web site could improve.  My goal is to begin a dialog across campus and to interest those with far deeper knowledge of computing in being part of a team, a team that will help spread the word and views of Vermont landscapes far and wide as both science education and research tools.</p>
