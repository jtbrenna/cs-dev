<h3>Seminar Description</h3>

<h4 class="subtitle">"Subsurface Biological System Simulation Using Cellular Automata"</h4>

<p><strong><a href="http://www.georgepinder.com/">George F. Pinder</a> (speaker) and Ganesh Oka</strong><br>
College of Engineering and Mathematical Sciences, The University of Vermont</p>
<p><strong>December 1, 2008</strong><br>
12:20 - 1:15 pm<br>
367 Votey Hall</p>

<p><strong>Summary</strong>
  <br />
  The growth in bacterial biomass in the subsurface brings about a change in hydraulic
conductivity of the host soil which, in turn, changes the fluid flow behavior and thereby
the distribution pattern of the contaminant as it undergoes biodegradation. The modeling
of growing biomass can be studied using cellular automata (CA) at the pore level. On the
other hand, the modeling of contaminant distribution is based on balance equations
formulated at the porous medium scale obtained by averaging extensive quantities, like
mass, at the pore level,. The momentum balance equation at the porous medium scale
involves terms describing the resistance offered to the flow of water as it passes through
the porous medium. The CA is specifically designed to calculate the growth and
resistance offered by solid surface-biomass interaction. Thus it is possible for the CA to
provide an input at the microscopic level to be used to define terms in the macroscale
momentum balance equation that describe the resistance offered to the flow of water;
thus changing the hydraulic conductivity of the soil. The resulting equations, which
bridge three physical scales, provide the mathematical foundation upon which a
biodegradation simulation model is constructed. Computed results show the feasibility of
this approach in biodegradation simulation.</p>

