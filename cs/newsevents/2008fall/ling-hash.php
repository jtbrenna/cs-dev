<h3>Seminar Description</h3>

<h4 class="subtitle">"Constructions for Hash Families"</h4>

<p><a href="http://www.cs.uvm.edu/%7Ealing"><strong>Dr. Alan Ling</strong></a><br>
Dept. of Computer Science, University of Vermont</p>

<p><strong>November 3, 2008</strong><br>
12:20 - 1:15 pm<br>
367 Votey Hall</p>

<p><strong>Summary</strong>
<br>
A <em>perfect hash family PHF(N; k, w, t)</em> is an <em>N</em> x <em>k</em> array on <em>w</em> symbols, in which in every <em>N</em> x <em>t</em> subarray, at least one row consists of distinct symbols. In this talk, we give various constructions for perfect hash families and its generalizations. In addition, various old and recent applications of hash families are discussed.
</p>

<p><strong>Speaker Bio</strong>
<br>
Professor Ling is an Associate Professor at the Department of Computer Science in the University of Vermont. He has published papers in various journals, including, IEEE Trans. on Inform. Theory, IEEE Trans. on Computer, Bioinformatics, SIAM J. on Discrete Math., and Journal of Combinatorial Designs.
</p>

