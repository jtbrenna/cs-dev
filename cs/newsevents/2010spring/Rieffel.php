<h3>Seminar Description</h3>

<h4 class="subtitle">"Mechanisms as Minds: What Camping Tents and Caterpillars Can Teach Us About Soft Robotics"</h4>

<p><strong>John Rieffel, Ph.D.</strong><br>
Computer Science Department<br />
Union College<br />
Schenectady, NY</p>


<p><strong>March 22, 2010</strong><br />
12:50 - 1:40 pm<br>
 367 Votey Hall</p>

<p><strong>Summary</strong>
<br>
Conventionally, roboticists have chosen animals with relatively rigid morphologies, such as dogs and cockroaches, as a &nbsp;source of 
inspiration. &nbsp; This choice is natural and convenient, because the  
underlying biomechanics are relatively well understood, and because 
rigid-bodied robots are relatively easy to design, build, and control.
<p>
 There are, however, inherent limitations to these designs: &nbsp;they
cannot quickly change shape, they cannot navigate uneven terrain, and
they cannot squeeze through holes and cracks. &nbsp;In order to build a new
generation of robots without these limitations, engineers must cast
their net wider, to the realm of soft-bodied animals, such as octopi
and caterpillars. &nbsp;Unfortunately, soft bodied systems, both natural
and artificial, are rife with the kinds of complex dynamics, such as
resonance, anisotropy, and dynamical coupling, that engineers strive
to avoid. &nbsp;In my talk I will argue that dynamical complexity can be a
boon rather than a bane to robot designers, and that, paradoxically,
as some systems become more mechanically complex they become easier to
control. &nbsp; I will present several examples of robotic systems in which
mechanisms act as minds, and bodies act as conduits for information. <p>Aside from helping us better understand the biomechanics of
completely soft animals such as caterpillars, these ideas are crucial
for developing control schemes for the emerging field of soft
robotics.</p>
