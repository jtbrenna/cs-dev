<h3>Seminar Description</h3>

<h4 class="subtitle">"A Computational Evolution System for the Genetic Analysis of Complex
Diseases"</h4>

<p><a href="http://epistasis.org/jason.html" target="_blank"><strong>Jason Moore</strong></a><br>
Dartmouth Medical School</p>

<p><strong>February 10, 2010</strong><br>
12:50 - 1:40 pm<br>
367 Votey Hall</p>

<p><strong>Summary</strong>
<br>
The initiation, progression and severity of common human diseases are complex processes that are dependent on many genes, many environmental factors and chance events that are perhaps not measurable with current technology or are simply unknowable. Success in the design and execution of population-based association studies to identify those genetic and environmental factors that play an important role in human disease will depend on our ability to embrace, rather that ignore, complexity in the genotype to phenotype mapping relationship for any given human ecology.  We review here three general computational challenges that must be addressed.  First, data mining and machine learning methods are needed to model nonlinear interactions between multiple genetic and environmental factors.  Second, filter and wrapper methods are needed to identify attribute interactions in large and complex solution landscapes.  Third, visualization methods are needed to help interpret computational models and results. We provide here an overview of computational methods that have been developed for addressing each of these challenges and discuss the broader impact that these methods will have in other areas of bioinformatics.
</p>
<p><strong>Speaker Bio</strong>
<br>
Dr. Moore is the Frank Lane Research Scholar in Computational Genetics, Professor of Genetics, Professor of Community and Family Medicine, and Director of Bioinformatics at Dartmouth Medical School. He holds adjunct appointments in the Departments of Computer Science at the University of New Hampshire and the University of Vermont. His interdisciplinary training in biological sciences (B.S., Florida State), statistics (M.A., University of Michigan) and human genetics (M.S., Ph.D., University of Michigan) provides an important foundation for communicating with experimental biologists and quantitative biologists. He serves as Director of the Dartmouth Integrative Biology Shared Technical Resource.
</p>

