<h3>Seminar Description</h3>

<h4 class="subtitle">The Dynamics of Change in Global Energy Systems
</h4>

<p><strong>Rajan Gupta, Ph.D.</strong><br />
  Nuclear and Particle Physics, Astrophysics and Cosmology Group T-2<br />
  Los Alamos National Laboratory</p>


<p><strong>April 5, 2010</strong><br />
12:50 - 1:40 pm<br>
 367 Votey Hall</p>

<p><strong>Summary:</strong></p>

Energy systems are extremely complex and different countries are evolving in different ways depending on their needs, finances and resource constraints. These differences have made it very hard to develop a cohesive international framework to address climate change while addressing the need for cheap energy.  This talk will give an overview of a collaborative web based tool -- <a href="http://GlobalEnergyObservatory.org">the Global Energy Observatory (GEO)</a> -- developed to understand global energy systems from bottom up. Our goal is to integrate real time analysis tools with the database to help better planning. We also seek feedback on the possibility of developing an undergraduate curriculum on the energy-development-environment-climate challenge as part of this effort.

<p><strong>Speaker bio:</strong></p>
Rajan Gupta is a theoretical physicist at Los Alamos National Laboratory and a Laboratory fellow.  He came to the USA in 1975 after obtaining his Masters in Physics from Delhi University, India, and earned his Ph.D. in Theoretical Physics from The California Institute of Technology in 1982. The main thrust of his research is to understand the fundamental theories of elementary particle interactions, in particular the interactions of quarks and gluons and the properties hadrons composed of them. In addition, he uses modeling and simulations to study Biological and Statistical Mechanics systems, and to push the envelope of High Performance Computing. He has published over 125 research papers in prestigious refereed journals and is a fellow of the American Physical Society. Click here to see a more detailed <a href="http://t8web.lanl.gov/people/rajan/">CV</a> and links to his interests and activities.<br><br />
  During 2007 he served as the chair of the LANL energy council. In this role he helped create a strategic plan for LANL's investment in energy R&D and advocated for energy security to be made a part of the core LANL mission. He is currently building a web based Global Energy Observatory that will provide a comprehensive view of the world's energy systems and their networks and interdependencies. Based on this understanding and database he is carrying out a global analysis (integrating demographics, economics, health, education and energy and water resources) to help accelerate the transition to cheap and clean energy for the global population.
