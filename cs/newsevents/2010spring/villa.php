<p><strong>Summary</strong>
<br>
Because only ad-hoc models can reflect local conditions with enough detail
to support decision making, useful predictive models must be adapted to a
highly diverse set of contexts, based on spatial and temporal locations as
well as local conditions expressed as data and expert opinion. I will
describe a novel approach, rooted in modern artificial intelligence, to
define model meta-structures that can be used to automate the construction
of ad-hoc models of variable structure, based on a set of context
observations and on rules that encode the expert knowledge that associates
model structure to specific environmental, social and economic conditions.
As an application, I will briefly demonstrate the ARIES (Artificial
Intelligence for Ecosystem Services) technology, a large-scale project aimed
to supporting environmental decision-making by quantifying and spatially
mapping the flows of benefits provided by natural systems to humans.control schemes for the emerging field of soft
robotics.</p>
