<p><strong>Abstract</strong><br />

Because of the vital importance of electricity to modern society and
because of a recent New York Times article [1] (motivated by results
described in [2]) vulnerability in the electricity infrastructure is a
major national, and international concern.  In this talk I will cover
three topics. First, I will describe some trends in the history of
large blackouts in North America. Second, I will discuss the
mechanisms of cascading failure in power grids, particularly focusing
on their susceptibility to random failures and directed attacks.  In
these results I will show how some graph theoretic models of grid
failure can lead to misleading conclusions. Finally, I will describe
decentralized strategies for mitigating cascading failure risk,
focusing in particular on the design and testing of "reciprocally
altruistic" control agents (inspired by vampire bats) that can respond
quickly and adaptively to problems in a network as they develop.
Results indicate that this method can reduce the sizes of many large
cascading failures.
<p>[1] J. Markoff and D. Barboza, &quot;Academic Paper in China Sets Off Alarms in U.S.,&quot; The New York Times, p. A10, March 21 2010.<br />

[2] J.-W. Wang and L.-L. Rong, &quot;Cascade-based attack vulnerability on the us power grid,&quot; Safety Science, vol. 47, pp. 1332-1336, 2009.<br />

<p><strong>Speaker Bio:</strong></p>

Paul Hines is an Assistant Professor in the School of Engineering at
the University of Vermont. He is also a member of the Carnegie Mellon
Electricity Industry Center Adjunct Research Faculty and a
commissioner for the Burlington Electric Department. He received the
Ph.D. in Engineering and Public Policy from Carnegie Mellon U. in 2007
and the M.S. degree in Electrical Engineering from the U. of
Washington in 2001. Formerly he worked at the US National Energy
Technology Laboratory, where he participated in Smart Grid research,
the US Federal Energy Regulatory Commission, where he studied
interactions between nuclear power plants and power grids, Alstom
ESCA, where he developed load forecasting software, and for Black and
Veatch, where he worked on substation design projects. His main
research interests are in the areas of complex systems and networks,
the control of cascading failures in power systems and energy security
policy.
