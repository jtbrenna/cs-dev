<h3>Seminar Description</h3>

<p><h4 class="subtitle">&quot;A Transactional Framework for Programming Wireless Sensor/Actor Networks&quot;</h4>
</p>

<br />

<p><strong><a href="http://www.cse.buffalo.edu/~demirbas/">Murat Demirbas</a></strong><br />
Computer Science and Engineering Department, SUNY Buffalo</p>

<strong>April 30, 2007</strong> <br />
12:20 - 1:10 pm<br />
Votey Hall, Rm. 367  
<p><strong>Summary</strong>
<br />
Effectively managing concurrent execution is one of the biggest challenges for future wireless sensor/actor networks (WSANs): For safety reasons concurrency needs to be tamed to prevent unintentional nondeterministic executions, on the other hand, for real-time guarantees concurrency needs to be boosted to achieve timeliness. In this talk, I will describe a transactional programming framework for WSANs that enables understanding of a system execution as a single thread of control, while permitting the deployment of actual execution over multiple threads distributed on several nodes. I will explain how we achieve a lightweight and decentralized implementation of this transactional framework by exploiting the properties of the wireless broadcast communication.</p>

<p><strong>Speaker Bio</strong>
<br />
Murat Demirbas received his Master's and Ph.D. degrees from The Ohio State University in 2000 and 2004, respectively. While at the Ohio State University Murat was involved in the development and deployment of a 100-node wireless sensor network, "Line in the Sand," for detection, classification, and tracking, which paved the way to the "ExScal" network with 1000 nodes. After a one-year post-doc with the Theory of Computing Group at MIT, Murat is currently an Assistant Professor at the Computer Science and Engineering Department at SUNY Buffalo. His research interests are in the areas of wireless sensor networks, distributed systems, and fault tolerance.</p>
