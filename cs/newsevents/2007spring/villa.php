<p><strong>Summary</strong>
<br />
The discipline of Ecoinformatics integrates environmental and information sciences, aiming for a synthesis of ecological knowledge that is amenable to formal description and automated processing. By defining protocols for semantic description of ecological data and models, and enabling machine reasoning on such knowledge structures, new levels of integration and sophistication in environmental applications can be reached. This has direct consequences on the potential of Ecoinformatics to support sensible, safer and easier environmental management. After an introduction to the discipline and a brief mention of the most important projects being carried out at the Ecoinformatics Collaboratory of UVM, I will describe our extensible, open source semantic modeling toolkit (Thinklab), and its role in our ecoinformatics projects. Among the latter, I will introduce in more detail the ARIES project &#151; a new, NSF-funded activity that will enable computer-assisted assessment and evaluation of ecosystem services, a key phase of the efficient and sustainable management of natural resources.</p>

