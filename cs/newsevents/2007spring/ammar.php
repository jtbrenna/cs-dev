<p><strong>Summary</strong>
<br />
Scheduling a large number of high-performance computing applications on cluster environments is a serious obstacle to achieving good performance. This becomes more critical in real-time systems. A cluster scheduler without enough knowledge of the state of the cluster and the scheduled tasks cannot adequately manage the cluster resources. Consequently, it may fragment the available processing power. This may cause rejection of some submitted applications due to tasks missing their deadlines.</p>
<p>
Although, researchers have investigated the problem of scheduling real-time task graphs, they have not provided mechanisms to satisfy the performance requirements while maximizing the processing power utilization.</p>
<p>
In this work, we focused on developing and evaluating scheduling techniques for real-time applications on cluster computing systems. These applications are represented as task graphs of different structures (tandem, fork join structure, conditional structure and loops) that arrive randomly to different nodes of the cluster. We have shown that using applications' task graphs give better results than treating each application as a single task unit.</p>

