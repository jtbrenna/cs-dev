<p><h4 class="subtitle">&quot;Genome-Wide Genetic Analysis Using Genetic Programming: The Critical Need for Expert Knowledge &quot;</h4></p>

<p><a href="http://www.epistasis.org/jason.html"><strong>Jason H. Moore</strong></a><br />
Dartmouth College</p>

<p>
<strong>  March 26, 2007</strong><br />
12:20 - 1:10 pm<br />
Votey Hall, Rm. 367</p>

<p><strong>Summary</strong>
<br />
Human genetics is undergoing an information explosion. The availability of chip-based technology facilitates the measurement of thousands of DNA sequence variation from across the human genome. The challenge is to sift through these high-dimensional datasets to identify combinations of interacting DNA sequence variations that are predictive of common diseases. The goal of this study is to develop and evaluate a genetic programming (GP) approach to attribute selection and classification in this domain. We simulated genetic datasets of varying size in which the disease model consists of two interacting DNA sequence variations that exhibit no independent effects on class (i.e. epistasis). We show that GP is no better than a simple random search when classification accuracy is used as the fitness function. We then show that including pre-processed estimates of attribute quality using Tuned ReliefF (TuRF) in a multi-objective fitness function that also includes accuracy significantly improves the performance of GP over that of random search. This study demonstrates that GP may be a useful computational discovery tool in this domain. This study raises important questions about the general utility of GP for these types of problems, the importance of data pre-processing, the ideal functional form of the fitness function, and the importance of expert knowledge.</p>

