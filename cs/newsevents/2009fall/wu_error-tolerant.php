<h3>Seminar Description</h3>

<h4 class="subtitle">"Error-Tolerant Data Mining"</h4>

<p><a href="http://www.cs.uvm.edu/~xwu/home.html"><strong>Xindong Wu</strong></a><br>
Dept. of Computer Science, University of Vermont</p>

<p><strong>November 23, 2009</strong><br>
12:50 - 1:40 pm<br>
367 Votey Hall</p>

<p><strong>Summary</strong>
<br>
Data mining seeks to discover novel and actionable knowledge hidden in data. As dealing with large, noisy data is a defining characteristic for data mining, where the noise in a data source comes from, whether the noisy items are randomly generated (random noise) or they comply with some types of generative models (systematic noise), and how we use these data errors to boost the succeeding mining process and generate better results, are all important and challenging issues that existing data mining algorithms can not yet directly solve.  Consequently, systematic research efforts in bridging the gap between the data errors and the available mining algorithms are needed to provide an accurate understanding of the underlying data and to produce enhanced mining results for imperfect, real-world information sources. This talk presents our recent investigations on bridging the data and knowledge gap in mining noisy information sources.</p>
<p><strong>Speaker Bio</strong>
<br>
Xindong Wu is a Professor and the Chair of the Department of Computer Science at the University of Vermont. His research interests include data mining, knowledge-based systems, and Web information exploration. Dr. Wu is the founder and current Steering Committee Chair of the IEEE  International Conference on Data Mining (ICDM), the founder and  current Editor-in-Chief of Knowledge and Information Systems (KAIS, by  Springer), the Founding Chair (2002-2006) of the IEEE Computer Society  Technical Committee on Intelligent Informatics (TCII), and a Series  Editor of the Springer Book Series on Advanced Information and  Knowledge Processing (AI&amp;KP). For more details about his work, <a href="http://www.cs.uvm.edu/~xwu/home.html">visit Dr. Wu's  website</a>.</p>

