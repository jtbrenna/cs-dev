<h3>Seminar Description</h3>

<h4 class="subtitle">"Innocent Until Proven Guilty: Reducing Robot Shaping from Polynomial
to Linear Time"</h4>

<p><a href="http://www.cs.uvm.edu/~jbongard/"><strong>Josh Bongard</strong></a><br>
Dept. of Computer Science, University of Vermont</p>

<p><strong>November 9, 2009</strong><br>
12:50 - 1:45 pm<br>
367 Votey Hall</p>

<p><strong>Summary</strong>
<br>
<img class="alignright" src="../gfx/bongard_reducing-robot-shaping.jpg" alt="Reducing Robot Shaping from Polynomial to Linear Time" title="Reducing Robot Shaping from Polynomial to Linear Time" width="300" height="174" />One of the main criticisms of evolutionary algorithms is that much time is spent evaluating poor solutions. In this talk I will be presenting a method for greatly improving the computational efficiency of evolutionary algorithms by terminating solutions early when they cannot compete with the current set of offspring-producing solutions in the population. I will show how this method is independent of the fitness function used; it only stops those solutions guaranteed to become inferior to the current offspring-producing solutions, and realizes significant time savings across several evolutionary robotics tasks including legged locomotion and object manipulation. Finally, I will show that for many tasks, running time was reduced from polynomial to sub-linear time, and time savings increased with the number of training instances used to evaluate a solution as well as with task difficulty.</p>
