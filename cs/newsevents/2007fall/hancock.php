<h3>Seminar Description</h3>

<p><h4 class="subtitle">&quot;An On-Ramp to Computational Fluency&quot;</h4>
</p>

<p><strong>Chris Hancock</strong><br />
<a href="http://www.tertl.com">Tertl Studos</a></p>

<p><strong>Sept. 17, 2007</strong> <br />
12:20 - 1:10 pm<br />
Votey Hall, Rm. 367</p>  
<p><strong>Summary</strong>
<br />
A major reason programming is hard for novices is that it is hard for everybody. Novices and experts alike struggle to organize programs for dynamic interaction with a changing environment. Novices and experts alike spend too much of their time trying to figure out what their own program is doing. In real-time programming these problems are, if anything, more difficult. And yet only real-time programming provides adequate expressive control of interactive media. Though notoriously difficult, real-time programming offers learners rich domains of application and can enlist bodily knowledge and experience in learning and creativity.
</p>
<p>
This talk presents efforts to address fundamental problems in the learnability of programming by means of two innovations: 1) a language in which declarative and procedural descriptions of computation are given equal status, and can subsume each other to arbitrary levels of nesting, and 2) a "live text" environment in which real-time display of, and intervention in, program execution are accomplished within the program text itself. Based on learners' use of this environment, as well as comparative evidence from other media and domains, the case is made that the seemingly specialized problems of real-time programming can be seen as keys to longstanding difficulties of programming in general, and, in particular, that the coordination of discrete and continuous processes should be considered a central Big Idea in programming and beyond. 
</p>
<p>
By changing the cost-benefit equation of computer programming—making it easier to learn, more creatively empowering, and more conceptually rich—this work aims to engage large numbers of young people in the core ideas of computational fluency, help reverse the recent plunge in college computer science enrollment; and unleash a new wave of creative and learning energy among young people.
</p>

<p><strong>Speaker Bio</strong>
<br />
Chris Hancock develops computer-based tools and games for learning, and studies learners' engagement with them. Over a 24-year career &#151; at Harvard's Educational Technology Center, TERC, Inc, the MIT Media Laboratory and, most recently, the new company Tertl Studos &#151; he has used this approach to explore ways of understanding computer programming, data analysis and statistics, discrete mathematics and algebra. He produced the classroom data visualization tools InspireData (formerly Tabletop) and Tabletop Jr., and co-created the acclaimed Zoombinis math games. His PhD research at MIT focused on tools for learning programming in the context of educational robotics.</p>

