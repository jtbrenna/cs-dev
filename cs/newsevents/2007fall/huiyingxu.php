<p><strong>Summary</strong>
<br />
Accurately characterizing the interaction of light with object surfaces is essential in realistic image synthesis. Natural materials can be classified into three types: opaque, transparent, and translucent. An opaque object only involves reflection, a transparent object involves both reflection and transmission, and a translucent object involves volumetric scattering as well as reflection and transmission at the object surface.
</p>
<p>
On reflection, we present a framework of spectral bidirectional reflectance distribution function (BRDF) measurement, data processing, compact representation, and spectrally-based image rendering. We describe two approaches for experimental measurement. Given the measured data, we develop a numerical method for noise-filtering, data resampling, and interpolation. Then we propose a method to represent compactly the spectral BRDFs in both spectral and spatial domains. Finally, we implement the represented and original spectral BRDF for image rendering, and give the comparison. 
</p>
<p>
On transmission, we present a physically based analytical model for light transmission at rough surfaces, and develop a Monte Carlo method to simulate light transmission process. We consider two types of light transmission process: single and multiple scattering. We compare the analytical model with the previous simulation and ours, and implement the analytical model for rendering images of transmission phenomenon.
</p>
<p>
On subsurface scattering, we present an approach to integrate both the surface and subsurface scattering. Based on the splitting rule, we decompose a translucent object with a rough surface into a homogeneous medium with a rough surface and an inhomogeneous medium with a perfectly smooth surface. We implement this approach for rendering translucent objects based our improved two-pass rendering technique, and discuss the contributions of surface and subsurface scattering, respectively.
</p>

<p><strong>Speaker Bio</strong>
<br />
Huiying Xu has received two doctoral degrees. One is PhD in Computer Science from Purdue University in May 2007. The other is a PhD in Physics from the Institute of Physics, Chinese Academy of Sciences, in 1999. Currently he is a post-doctoral research associate, working on parallel computation. He also has research experience in computer graphics, scientific visualization, computational neuroscience, virtual environment modeling, condensed matter physics theory, and non-equilibrium statistical fracture mechanics. </p>

