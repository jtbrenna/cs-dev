Votey Hall, Rm. 367  
<p><strong>Summary</strong>
<br />
Longest Common Subsequences for any two given linear strings have a simple and well-known definition. This has many applications in many different areas such as spell-checking, speech-recognition, study of bird-singing, finding patterns in musical data, finding similarities in DNA sequences, and finding common trajectories of moving bodies. Many objects &#151; such as 2D images and 3D protein structures &#151; can be represented as multi-dimensional strings. Unfortunately, similarity for strings in dimensions higher than one is difficult to define. In this talk, I present a definition based on pairwise point-matching with constraints. Computing a largest common subsequence in this definition has several interesting formulations which are all NP-hard. A special case of the problem for two-dimensional strings has a dynamic programming solution. The talk will be centered at theoretical aspects of the definition and the resulting computational problems. Experimental results are not available yet.</p>

<p><strong>Speaker Bio</strong>
<br />
Dr. Abdullah Arslan earned his PhD degree in Computer Science in 2002 from the University of California at Santa Barbara. His main research interest is in developing algorithms on strings. A major application area of these algorithms is bioinformatics.</p>

