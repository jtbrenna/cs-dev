<p><strong>Summary</strong>
<br />
Ecological network analysis allows for an investigation of the structural and functional interactions in ecosystems. Typically, these interactions are seen to comprise a food web of "who eats whom,"  but more generally this applies to the transfer of energy-matter within the biotic and abiotic ecosphere.  This web of transactions between system components leads to an overall network structural pattern depicted as either a digraph or an adjacency matrix. Regimes can be categorized based on their structural,  such as cycling, or functional, such as qualitative relationships, characteristics.  This presentation demonstrates threshold effects of the placement or removal of links,  such that certain changes essentially keep the system in the same regime whereas others shift it to another regime in a non-linear manner.
</p>

