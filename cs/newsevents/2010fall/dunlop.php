<p><strong>Abstract:</strong><br />
Biological systems use feedback to respond to changing conditions and
optimize the use of resources. In this talk I will discuss how we can
engineer feedback loops in microorganisms to produce novel function. After a
brief survey of the field of synthetic biology, I will discuss two examples:
a feedback circuit for improving microbial biofuel production and a natural
example of a feedback loop that leads to antibiotic resistance. Using these
examples, I will discuss some of the open problems and research challenges
in designing biological control systems, and in understanding the role of
natural feedback loops.

<strong>Speaker Bio:</strong>
Dr. Dunlop graduated from Princeton University in 2002 with a B.S.E. in
Mechanical and Aerospace Engineering and a minor in Computer Science.
She received her Ph.D. in 2008 from the California Institute of
Technology, where she studied dynamics and noise in gene regulation at
the single-cell level. She then conducted postdoctoral research on
biofuel production at the Dept. of Energy's Joint BioEnergy Institute
and Lawrence Berkeley Lab. In 2010, Dr. Dunlop joined the faculty of the
School of Engineering at UVM where she leads a group studying feedback
and dynamic regulatory responses in microbes.
