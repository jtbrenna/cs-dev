<h4>Requirements</h4>
<p>For the Bachelor of Science, Major in Computer Science (BS[CS]), the requirements for Catalogue 2010-11 are a minimum of 120 credits as follows:</p>
<UL>
	<li> Computer Science (44 credits): one introductory programming course chosen from 16, 21, or equivalent;
  with core: 64, 110, 121, 123,
  124, 201, 224 or 243, and 292; plus 18 additional credits, including 15
  credits at the 200-level. No more than 60 credits of Computer Science can
  be applied to this degree.

  <BR>[The remaining 3 credits of the 18 additional credits
  may be at the 0XX level or above. Students with no programming
  experience may want to take a 3-credit course at the 0XX level with a
  programming component prior to CS 16 or 21.]
  See <A href="http://www.uvm.edu/~cems/cs/?Page=undergrad/bscs/10-11_bscs.php&amp;SM=undergrad/_undergradmenu.html#sequence"
 >Course
  Sequence</A>.]
	<li> Mathematics (14 credits): 21, 22, two of (121, 124, 173, 271)
	<li>        Statistics (3 credits): 153
    <li> Natural Science (13 credits): chosen from courses in Astronomy,
  Biology (or BioCore), Chemistry, Environmental Science,
  Geology, Microbiology and Molecular Genetics, Plant Biology, or
  Physics, including one of the following laboratory science sequences:
  <BR>
  <ul>
	<li> BIOL 1 (or BCORE 11), and BIOL 2 (or BCORE 12);
	<li> CHEM 31 (or 35), and 32 (or 36);
	<li> PHYS 51 and 152. (31/125 sequence is an acceptable subsitution).
	<BR>[Note: Certain minors require specific science courses.]
	</li>
  </ul>
  <li> Writing (3 credits): English 1, 50, or 53
  <li> Fine Arts, Humanities, and Social Sciences (18 credits):
  Eighteen additional credits chosen from courses in ALANA Studies, Anthropology,
  Area and International Studies, Art History, Art Studio, Classics,
  Communication Sciences, Dance, Economics, English, Film and Television Studies,
  Foreign Language, Geography, History, Holocaust Studies, Linguistics, Music,
  Philosophy, Political Science, Psychology, Religion, Sociology,
  Theatre, Women's and Gender Studies, and World Literature.

  <li> [See UVM diversity requirement: <A href="http://www.uvm.edu/academics/catalogue2010-11/?Page=allpolicies.php&SM=policymenu.html&policy=Degree%20Requirements%20for%20Undergraduates"
 >General Requirements</A>.  Credits used to fulfill the UVM diversity
  requirement can also satisfy other distribution requirements.]

  <li>MINOR: Students must complete a <a href="http://www.uvm.edu/academics/catalogue/?Page=alldegrees.php&amp;SM=offeringmenu.html&amp;letter=a&amp;type=minors">University-approved minor</a>
     (excluding Computer Science); courses used to fulfill other requirements may be used
     to satisfy minor requirements.
  <li>Free Electives (19 credits no PEAC) to reach the minimum of 120 credits.
 </UL>
  <p>
<strong>Grade Policy for BS[CS]</strong><br >
No more than three grades of D+, D, or D- in CS courses numbered 123 and higher.
