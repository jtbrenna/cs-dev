<h4>Requirements</h4>
<p>For the Bachelor of Science, Major in Computer Science (BS[CS]), the requirements for Catalogue 2003-04 are a minimum of 124 credits (122 for students exempt from PEAC requirement) as follows:</p>
<ul>
	<li>Computer Science: 21, 26, 100, 101, 103, 104, 201, 224 or 243, 292, plus 15   additional credits (5 courses) in 200-level courses not more than three credits of which may be independent study. </li>
	<li>Mathematics: 21, 22, 54, two of (121, 124, 173, 271) </li>
	<li>Statistics: 141 or 211 ( 211 recommended), 151 </li>
	<li>Four of
	  <ul>
          <li>Biology: 1, 2 </li>
	    <li>Chemistry: 31, 32 </li>
	    <li>Physics: 31 with 21; 42 with 22 <br />
	      <em><strong>Note:</strong> Specific science courses are required for certain minors.</em> </li>
      </ul>
  </li>
	<li>English 1 </li>
	<li>6 credits (2 courses) from Social Science selected from: ALANA, Anthropology, Economics, Geography, History, Political Science, Psychology, Sociology, Women's Studies, or other advisor-approved electives </li>
	<li>6 credits (2 courses) from Humanities and Fine Arts selected from: Art, Drama, Film, Language, Literature, Music, Philosophy, Religion, Speech or other   advisor-approved electives </li>
	<li>15 additional credits in advisor-approved courses in Humanities, Social   Sciences, and Arts, to include either AH 95, AGRI 95, or one course approved by the College of Arts and Sciences as a course in non-European cultures or Race Relations and Ethnicity, as specified under General Requirements in the College of Arts and Sciences section of this catalogue. </li>
	<li>12 additional credits in advisor-approved free electives (excluding PEAC). </li>
	<li>PEAC: Two credits of Physical Education Activities. </li>
	<li>MINOR: Students must complete an official minor offered by the university   (excluding Computer Science); courses used to fulfill other requirements may be used to satisfy minor requirements.</li>
</ul>
<p>
<strong>Grade Policy for BS(CS)</strong><br />
No grade below a C- in any computer science course will be accepted, except as free elective credit.</p>
