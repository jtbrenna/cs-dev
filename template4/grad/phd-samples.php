<h3><span class="insideh3">Graduate Program</span><br />
Sample Programs of Courses for the PhD</h3>

<p>The curricular requirements of our PhD program are flexible enough to accommodate students with varying backgrounds and interdisciplinary interests. Students will work closely with their graduate studies committee to design curricula that are commensurate with their backgrounds and interests. The five sample programs shown below illustrate the breadth of programs possible.</p>
<p><em><strong>Please Note</strong>:</em> <em>These samples are selected to demonstrate this breadth and do not necessarily represent a typical cross-section of our expected students. The courses used in the samples may or may not be suitable for a particular student, and may or may not be available during the specified semesters.</em></p>
<p><strong>The studies committee of a PhD student will define the program of the student.</strong></p>

<br />

