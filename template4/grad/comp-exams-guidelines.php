<h3>Computer Science Comprehensive Exams Guidelines</h3>

<p><em>Updated on November 16, 2009</em></p>

<p class="h5emphasis">1. Required areas/courses to acquire a core body of knowledge in Computer Science</p>

<p><strong>1.1</strong><br>
All Master's students are expected to complete the following three
courses (see <a href="http://www.uvm.edu/~cems/cs/?Page=grad/ms-guide.php&SM=grad/_gradmenu.html#degreereqs">Master's Degree Requirements</a>):</p>

<ul>
<li>CS201 Operating Systems</li>
<li>CS224 Analysis of Algorithms</li>
<li>CS243 Theory of Computation</li>
</ul>

<p><strong>1.2</strong><br>
PhD students are required to take (a) CS224 Analysis of Algorithms, and (b) one course chosen from: Artificial Intelligence, Artificial Neural Networks, Data Mining, Evolutionary Computation.</p>

<p class="h5emphasis">2. MS comprehensive exams</p>

<p><strong>2.1</strong><br>
MS students who complete all their required courses at UVM with a grade of A- or better on each of them are considered to have passed the MS comprehensive exams.</p>

<p><strong>2.2</strong><br>
MS students who either took one or more required courses at another institution, or who passed a course at UVM but with a grade between B+ and C- must take an oral exam in this course area. In this event, the Graduate Committee will form an exam committee for this oral exam.</p>

<p>The committee for this oral exam will award students one of three outcomes to the oral exam:</p>

<ol class="alpha">
<li>Pass</li>
<li>Fail with opportunity for at most one retake</li>
<li>Fail without opportunity for retake</li>
</ol>

<p class="h5emphasis">3. PhD comprehensive exams</p>

<p>The Studies Committee of each PhD student administers the student's comprehensive exams with both oral and written components, with approval by the Graduate Committee.</p>

<p><strong>3.1</strong><br>
All students enrolled in the UVM CS PhD program must pass the PhD comprehensive exams, regardless of whether they received their MS degree at UVM.
</p>

<p><strong>3.2</strong><br>
The PhD comprehensive exams comprise a written component and an oral component.</p>

<p><strong>3.3</strong><br>
The written exam is given in the area of Algorithms. In case the student's performance is not satisfactory in this written exam, an optional follow-up oral exam may be called for by examiners. The examiners are Computer Science graduate faculty members appointed by the Graduate Committee.</p>

<p><strong>3.4</strong><br>
The oral exam is a single-session exam, and aims to examine a student's breadth of knowledge in selected topical areas. The topical areas and examiners on this oral exam committee will be determined by each student's Studies Committee, with approval by the Graduate Committee after a commenting period of one week from the CS graduate faculty. The specific policy on the oral examination procedure is administered by the Graduate Committee.</p>

<p><strong>3.5</strong><br />
The examination committee will award students one of five outcomes to the exam:</p>
<ol class="alpha">
<li>Pass at the PhD level</li>
<li>Pass at the MS level with opportunity for at most one retake (to try for a PhD level pass)</li>
<li>Pass at the MS level without opportunity to retake</li>
<li>Fail with opportunity for at most one retake</li>
<li>Fail without opportunity for retake at the PhD level</li>
</ol>

<p>PhD students who pass their PhD comprehensive exams at the MS level but not at the PhD level may, if desired, complete any remaining requirements to complete an MS degree (including the MS comprehensive exam, defined above), but are not allowed to advance to candidacy for the PhD.</p>

<p><strong>3.6 Timing of written and oral comprehensive exams</strong><br />
Written comprehensive exams are given by the Graduate Committee twice a year, in May and January.</p>

<p>Each student's Studies Committee will approve an appropriate timeframe of oral exams for a given student based on their individual circumstances. It is then up to the student to schedule their exams within the agreed-upon timeframe. While individual circumstances may vary, normal expectations are as follows:</p>
<ul>
<li>PhD students are normally expected to take oral exams by the end of their second year of full-time PhD graduate study (part-time students may take longer).</li>
<li>A student who needs to retake their oral exams is normally expected to do so within 6 months of their first attempt.</li>
</ul>

