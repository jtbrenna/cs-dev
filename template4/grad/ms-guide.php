<h4>Advisors and Their Research Interests</h4>

<p>Your Master's advisor serves as your mentor during your graduate
training. It is important that you feel comfortable discussing issues
with your advisor.</p>

<p>An advisor will be assigned to you when you initially enroll in the
program. Typically, your initial advisor will be the <a href=http://www.uvm.edu/~cems/cs/?Page=grad/ms-guide.php&SM=grad/_gradmenu.html#gradcoord>Director of Graduate Studies</a>. You
may request a change of advisor at any time, if you feel that another
graduate faculty member would better serve your needs. The new advisor
must agree to the change.</p>

<p>Thesis students must select a thesis advisor from among the
graduate faculty of the Department. The thesis advisor also serves as
the student's overall advisor (and thus replaces the initial advisor
or other advisors). Also, the chosen thesis advisor must agree to
serve as such.</p>

<p>Students should choose a thesis advisor based on three factors:</p>
<ul>
<li>common interest in a research problem,</li>
<li>comfortable working relationship, and</li>
<li>willingness of the advisor to advise the student.</li>
</ul>

<p>Students considering taking the thesis option should meet with all
potential advisors early in their graduate studies. Talk about
potential thesis topics to see if that person's work interests
you. Also try to see whether you would be comfortable working with
that person. The potential advisor should tell you whether they are
willing to take you on as a thesis student.</p>

