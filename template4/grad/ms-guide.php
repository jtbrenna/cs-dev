<h4>Master's Degree Requirements</h4>

<p>The Master's of Science in Computer Science degree is intended to
add depth in computer science to an undergraduate degree. Many
students use a Master's degree to further an existing career as a
computer professional; others use the program as an opportunity to
change career paths. Many of these students have majors in disciplines
other than computer science; students with particularly weak computer
science background will be required to take additional courses before
being accepted into the Master's program. Each student will be
evaluated regarding additional courses (in addition to the MS degree
requirements) to be taken. These additional course requirements are
detailed in the acceptence letter from the graduate college.</p>

<h5>Core Course Requirement</h5>

<p>The degree program can be completed with one of the three options
(thesis, project, and coursework, see below). Regardless of the option
chosen, all Master's students are expected to complete the following
three courses:</p>

<ul>
  <li>CS201 Operating Systems</li>
  <li>CS224 Analysis of Algorithms</li>
  <li>CS243 Theory of Computation</li>
</ul>

<p>Based on a student's background, the Graduate Committee may grant a
student a waiver from some or all of these course requirements. In the
case of a waiver, a replacement course needs to be approved by the
student's advisor and the Graduate Committee.</p>

<h5>Comprehensive Exams</h5>

<p>Each student must pass a comprehensive exam before graduating,
regardless of the option chosen. A complete description can be found
from the <a
href="http://www.uvm.edu/~cems/cs/?Page=grad/comp-exams-guidelines.php&SM=grad/_gradmenu.html">CS
comprehensive exam guidelines</a>. </p>

<p>Each student who needs to take comprehensive oral exams according
to the guidelines should arrange a schedule with the examiners and
then inform the Graduate Committee of the exam date. It is strongly
recommended that the examination is completed during the academic
year, unless all examiners agree to give the exam on a date during the
break.</p>

<p>Students must enroll in GRAD 397: Master`s Comprehensive
Examination prior to taking the comprehensive examination or being
considered to have passed the exam. There is no fee. A grade of
&quot;S&quot; or &quot;U&quot; is recorded.</p>

<h5>Credit Transfer</h5>

<p>With approval of the Graduate Committee, up to 9 credit hours of
course work in Computer Science (or a closely related field) may be
transferred into the Master's degree. Only credits that have not been
used for other degrees (Bachelor's, Master's, or others) are
considered. Note that these credits include any courses taken at UVM
before enrolling in the Master's program and any courses taken at
other institutions, whether before or after enrolling at UVM. In
addition, with the approval of the Graduate Committee, a student may
apply courses in a closely related field taken at the graduate level
while in residence at UVM to their Master's requirements.</p>

<p><em>Note that transfer and waiver are two different, independent
concepts.</em> A transfer means that the transferred credits are
counted towards your Master's degree at UVM, while a waiver only means
that one of the five <em>required</em> courses is replaced by some
other courses (possibly transferred ones).</p> <p>Also, please note
that the <a
href="http://www.uvm.edu/~cems/cs/?Page=grad/ms-guide.php&SM=grad/_gradmenu.html#amp">Accelerated
Master's Program (AMP)</a> (see below) is an exception to this
9-credit-transfer rule.</p>

<h5>Academic Standing</h5>

<p>To remain in good standing, students must continue to make
reasonable progress towards completing their degree requirements. To
measure this progress, three requirements are placed on all Master's
students:</p>

<ul>

<li><b>Time limit.</b> All full-time students (taking at least 15
credits/year) must complete their degree within 3 years of
enrollment. Part-time students must complete their degree with 5 years
of enrollment. Extensions may be awarded for extraordinary extenuating
circumstances, at the discretion of the Graduate Committee.</li>

<li><b>Grades in individual courses.</b> Any grade of B- or worse is
unacceptable for a Master's student. Upon receipt of a second (or any
subsequent) unacceptable grade, the student's progress will be
reviewed by the Graduate Committee. The Graduate Committee may choose
to impose sanctions on the student, including requiring an additional
course or dismissing the student from the program.</li>

<li><b>Accumulative grade point average (GPA).</b> Every Master's
student must maintain a 3.0 (B) GPA at all times. Any student falling
below a 3.0 will be placed on academic probation and their progress
will be reviewed by the Graduate Committee, with the possible
imposition of sanctions. Furthermore, no student whose final GPA is
below 3.0 will be allowed to graduate.</li> 
</ul>

<h5>Thesis Option</h5>

<p>Students choosing the thesis option must complete a minimum of 30
credit hours, including minimum 21 credits of coursework and 6 credits
of thesis research.</p>

<p>A Master's thesis consists of original research work done under the
guidance of a faculty member. Students opting to pursue a thesis must
select a thesis advisor who agrees to supervise that student's thesis
work. The thesis advisor may be any graduate faculty member who holds
either a primary or a secondary appointment from the Department.</p>

<p>Full-time students should normally choose a thesis advisor by the
end of their first semester. Prior to the selection of a thesis
advisor, a member of the Graduate Committee serves as the student's
advisor (see the Director of Graduate Studies for details).</p>

<p>Each thesis student must write a thesis describing their
research. The thesis is presented before a thesis committee in a
public oral thesis defense. The thesis committee must include three
different individuals: (1) the student's thesis advisor (see below),
(2) another graduate faculty member of the department, and (3) the
chair of the thesis committee. The chair of the thesis committee must
be a member of the graduate faculty without an appointment (either
primary or secondary) in the department.</p>

<p>At least three weeks before the defense, the written thesis must be
submitted to the Graduate College for a format check. At least two
weeks before the defense, the student must make copies of the written
thesis available to all members of the thesis committee. The thesis
defense itself must be adequately advertised to the community. 

<h5>Project Option</h5>

<p>Students choosing the project option must complete a minimum of 30
credit hours, including minimum 24 credits of coursework and 3 credits
of project (CS392). </p>

<p>A graduate project typically consists of a significant
implementation done under the guidance of a faculty member. Students
opting to pursue a project must select an advisor who agrees to
supervise that student's work. The advisor may be any faculty member
who holds either a primary or a secondary appointment from the
Department.</p>

<p>Full-time students should normally choose a project advisor by the
end of their first semester. Prior to the selection of an advisor, a
member of the Graduate Committee serves as the student's advisor (see
the Director of Graduate Studies for details).</p>

<p>The results of the project are presented before a project committee
in a public talk, which has been advertised to the community. The
project committee must include three individuals, at least two of whom
must hold appointments in the department. The chair, who may be the
project advisor, must be a member of the Graduate College and hold an
appointment in the department. The composition of the committee must
be approved by the Graduate Committee.</p>

<p>One bound copy of the project report should be submitted to the
Graduate Program Director within 30 days after the defense. The
submitted copy will be archived in the departmental file. </P>

<p>Documents needed to complete a project defense comprise the project
report, acceptance page, and the exam result. Templates of these
documents can be downloaded from the following links: <a
href="http://www.cems.uvm.edu/~bslee/gradinfo/MSprojectTemplates/ProjectReportFront_sample.pdf">
Project report front sample</a>, <a
href="http://www.cems.uvm.edu/~bslee/gradinfo/MSprojectTemplates/ProjectAcceptancePage_template.doc">
Project acceptance page template</a>, <a
href="http://www.cems.uvm.edu/~bslee/gradinfo/MSprojectTemplates/ProjectExaminationResult_form.doc">
Project examination result form</a>.</p>

