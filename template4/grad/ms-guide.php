<h4>Department Information</h4>

<p>This handbook contains information about the Master's Degree
Program in Computer Science at the University of Vermont. It should be
useful for potential students, current students, advisors and
professors. If you have any comments or suggestions, please relay them
to the <a href=http://www.uvm.edu/~cems/cs/?Page=grad/ms-guide.php&SM=grad/_gradmenu.html#gradcoord>Graduate Program Director</a>.</p>

<p> The Computer Science Department is hosted in the <a
href="http://www.uvm.edu/~cems/">College of Engineering and
Mathematical Sciences (CEMS)</a>.  The Department is located on the
third floor of Votey Hall, which is a brick building with long
vertical concrete stripes (including windows) located behind Billings
Student Union. The department office is located in Votey 351. The
majority of the faculty offices are most easily reached by passing
through the department office.</p>

<p>The Department offers <a
href="http://www.uvm.edu/~cems/cs/?Page=undergrad/default.php&amp;SM=undergrad/_undergradmenu.html">three
undergraduate degree programs</a>, two graduate degree programs and a
<a
href="http://www.uvm.edu/~cems/cs/?Page=undergrad/minor-cert.php&amp;SM=undergrad/_undergradmenu.html">certificate
program</a>. The undergraduate programs include a Bachelor's of
Science in Computer Science offered through CEMS, a Bachelor's Science
in Computer Science and Information Systems offered in conjunction
with the School of Business, and a Bachelor's of Arts majoring in
Computer Science offered through the College of Arts and Sciences.</p>

<p>The Department offers a Master's of Science (MS) in Computer
Science degree and a <a
href="http://www.uvm.edu/~cems/cs/?Page=grad/phd-guide.php&amp;SM=grad/_gradmenu.html">Doctor
of Philosophy (PhD)</a> in Computer Science degree.<span
class="style1"><i>
This document details the MS degree program</i>.</span></p>

<p>Many people will be important to a graduate student's career, but
two people are of immediate interest. <strong>Mary (Penni)
French</strong> is the Department Administrative Assistant. She sits
in the department office (Votey 351) and can be reached at (802)
656-3330. If you have an administrative question and are unsure whom
to ask, try Penni.  For more contact information, please see <a
href="http://www.uvm.edu/~cems/cs/?Page=welcome/contact.php&SM=welcome/_welcomemenu.html">Contact
Us.</a></p> 

<p>A departmental Graduate Committee is charged with drafting policies
and reviewing admission/graduation applications, among other
duties. Dr. Wu is the current chair of the committee, whose other
members are Dr. Josh Bongard, Dr. Byung Lee, and Dr. Michael
Radermacher (Molecular Physiology and Biophysics).
</p> 

<p> All computer science students are encouraged to join and be active
in the <a href="http://www.cems.uvm.edu/~cssa/">Computer Science
Student Association (CSSA)</a>. The CSSA schedules a mixture of social
and technical events throughout the school year. The CSSA technical
events generally focus on career opportunities and skills relevant to
computer science students.</p>


