<h5>Applying to the Master's Program</h5>

<p>A student interested in a Master's degree in Computer Science must
submit a complete application packet including</p>

<ul>
  <li>the application form
(available on-line at:<br /> 
<a href="http://www.uvm.edu/~gradcoll/?Page=admissions.html">http://www.uvm.edu/~gradcoll/?Page=admissions.html</a>)</li>
  <li>complete collegiate transcripts </li>
  <li>statement of purpose (available as part of the packet) </li>
  <li>three reference letters </li>
</ul>

<p> In addition, Graduate Record Exam (GRE) general scores must be
submitted.  There is no cutoff GRE score required for admission; the
Department considers GRE scores as one factor in a complete
application packet. Past academic performance, reference letters, the
statement of purpose and any other exceptional circumstances all help
determine the applicant's suitability for the program. We admit
students who we believe are most likely to succeed and thrive in the
program.
</p>

<p>Applicants whose native language is not English or whose formal
education has been conducted in a language other than English must
have a Test of English as a Second Language (TOEFL) score of 550 (or
213 with computer-based test or 79 with Internet-based test) or above.
To be considered for financial assistantship from the University,
applicants must have a TOEFL score of 600 (or 250 with computer-based
test or 100 with Internet-base test) or above.  </p>

<p>Application deadlines are:
</p>
<ul>
  <li><b>Fall enrollment:</b> Apply by February 15</li>
  <li><b>Spring enrollment:</b> Apply by October 15</li>
</ul>
<p>Applications received after the deadlines are considered on a case-by-case basis.
</p>

<h5>Master's Program Pre-Requisites</h5>

<p> The Master's program assumes all incoming students have the
fundamental skills required of computer science students, with strong
programming skills in C, C++ or Java and a solid mathematical
background, including calculus, statistics, probability and discrete
mathematics.  </p>

<p>Typically entering students can demonstrate this background through
previous course work. A solid background for the program would include
the following courses: </p>

<ul>
  <li>2 semesters of programming in a modern, high-level programming language (CS21 and CS110 at UVM)</li>
  <li>1 course in computer organization, including exposure to assembly language and computer arithmetic (CS121) </li>
  <li>1 course in theory of computation (e.g. CS 125)</li>
  <li>1 course in data structures (CS124)</li>
  <li>Differential, integral and multivariate calculus (MATH 21+22)</li>
  <li>Discrete mathematics (CS64)</li>
  <li>Linear algebra (MATH 124)</li>
  <li>Statistics and probability (STAT 141, 151, or 153)</li>
</ul>

<p>Many students lack one or two of these courses. In this case, your
admission letter may state requirements to supplant your background in
addition to the requirements listed in the <a
href="http://www.uvm.edu/~cems/cs/?Page=grad/ms-guide.php&SM=grad/_gradmenu.html#degreereqs">Degree
Requirements section</a>. Once these requirements have been completed,
you will be advanced to candidacy for the Master's Degree. Students
satisfying all the above requirements will normally be advanced to
candidacy upon being granted admission.

<p>All applicants must demonstrate a solid background before being
admitted.  Only applicants demonstrating at least the equivalent of
two of CS121, CS124, and CS125 and a reasonable background in mathematics
will be considered for admission.
</p>

<h5>Funding for Students</h5>

<p> The Department of Computer Science is able to fund a small number
of graduate students every year.</p>

<p>The most common form of funding is as a Graduate Teaching Assistant
(GTA). Each GTA spends 20 hours each week during the school year
(starting 1 week before classes begin) supporting the department's
educational mission. This work typically involves some combination of
grading, offering help sessions or office hours and monitoring student
labs. Each GTA will be assigned a specific number of hours for each
task and will work under the guidance of the faculty member teaching
the course or lab. Each GTA must also attend a series of short
training sessions early in their first semester.</p>

<p>The Graduate Committee nominates potential GTAs from the pool of
admitted students. GTAs are selected from the best students in the
program who best fit the department's needs. It is possible, to gain
GTA funding after one or more semesters as an unfunded graduate
student. GTA students who continue to satisfy the department's
requirements and make satisfactory progress towards their degree will
maintain their funding. </p>

<p> Funding as a Graduate Research Assistant (GRA) is also
available. The availability of research funding depends on the current
grants held by departmental faculty. Students with exception research
potential in the funded areas will be considered as GRAs. All admitted
students indicating interest in departmental funding will be
considered for all available funded positions. The only requirement of
the applicant is to indicate interest in departmental funding on the
application. </p>

