<h4>Uniqueness of the Group</h4>
<p>Many research CS departments have a component of distributed systems. However, most are concentrated on the networking aspects, or on particular applications. The uniqueness of this group is in its emphasis on "vertical slices" for distributed systems: from underlying hardware support (robots, cameras and sensors), to application-level research (security, distributed control and monitoring). That is, we target applications that use distributed systems, and at the same time, we study all the necessary tools and theories in the software to hardware levels to support the applications. Hence, part of the uniqueness of this group is its <strong>interdisciplinary</strong> nature.</p>

<h4>Interdisciplinary Connections</h4>
<p>We will find the use of distributed systems in many applications. Especially important are the connection of this group’s research with the two emphasized areas of UVM: environmental and healthcare studies. The connection with the ECE and Math departments is obvious. Also, we see a huge potential of distributed systems in the future Transportation Center. The Vermont Advanced Computing Center (VACC), now under construction, has also decided to emphasize on sensor systems. We believe our group can contribute a lot to this vision of VACC.</p>

