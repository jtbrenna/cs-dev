<h4>Vision</h4>
<p>We envision a world in which various kinds of systems, including sensors and actuators, mobile phones, PDAs, web servers and clients, and large databases, all work together to help human users to monitor environments, obtain/provide healthcare, make decisions, play games, and in general live and work. Computer Science research has a lot to contribute to this vision of the world, and faculty in the CS and ECE departments have involved in research in various aspects. More specifically,</p>
<ul class="newsevents">
<li>Sensor networks and their security </li>
<li>Security of distributed systems</li>
<li>Distributed databases and data stream processing</li>
<li>Data mining from distributed data</li>
<li>Multiple robotics and distributed visual computing</li>
</ul>

