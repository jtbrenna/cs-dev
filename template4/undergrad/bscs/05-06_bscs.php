<h4>Requirements</h4>
<p>For the Bachelor of Science, Major in Computer Science (BS[CS]), the requirements for Catalogue 2005-06 are a minimum of 124 credits   (122 for students exempt from PEAC requirement) as follows:</p>
<ul>
	<li> Computer Science (41 credits): 21, 26, 100,
    101, 103, 104, 201, 224 or 243, 292, and 15 additional credits (5 courses) of 200-level courses not more than three credits of which may be independent study.
	<li> Mathematics (17 credits): 21, 22, 54, two of (121, 124, 173, 271)
	<li>        Statistics (3 credits): 153
    <li>Science (13 credits): advisor-approved science electives in Astronomy,
  Anatomy &amp; Neurobiology, Biology, Botany, Chemistry, Environmental
  Science, Geology, Microbiology &amp; Molecular Genetics, Nutrition &amp; Food
  Sciences, Physics, or Electrical Engineering 3 or 4, to include 1 laboratory
  science sequence selected from the following:<BR>
  <ul>
		<li> Biology: 1 or 11, 2 or 12
		<li> Chemistry: 31 or 35, 32 or 36
	  <li>Physics: 31 with 21, 42 with 22 <BR>
         <em><strong>Note:</strong> Certain minors require specific science courses.</em></li>
  </ul>
  <li>Technical Electives (6 credits): Two courses in
  approved <a href="../../?Page=undergrad/bscs/05-06_bscs.php&amp;SM=undergrad/_undergradmenu.html#techelectives">technical electives</A>. See department for current list of approved courses.
  <li> Writing (3 credits): English 1, 50, or 53
  <li>HSS (18 credits): 6 courses in Social Science,
  Humanities, and Fine Arts electives. At least 6 credits must be chosen
  from the Social Science Group and at least 6 credits must be chosen from the
  Humanities and Fine Arts Group as defined below.  In addition, students
  must either complete a 1 credit Race &amp; Culture course or ensure that at
  least one HSS elective also satisfies the <A href="http://www.uvm.edu/academics/catalogue/?Page=read.php&amp;p=/Colleges_and_Schools/College_of_Arts_and_Sciences/Requirements/College_of_Arts_and_Sciences_Requirements_for_the_Bachelor_of_Arts&amp;SM=collegemenu.html"
 >non-European Cultures or Race
  Relations &amp; Ethnicity Requirements</A> , as specified under the General Requirements in the College
  of Arts and Sciences section of the university catalog.

<ul>
    <li>Social Science Group: ALANA, Anthropology,
    Economics, Geography, History, Political Science, Psychology, Sociology,
    Women's Studies, or other advisor-approved social science electives.
    <li>Humanities and Fine Arts Group: Art, Classics, Drama, Film, Language, Literature,
    Music, Philosophy, Religion, Speech or other advisor-approved humanities or
    fine arts electives.<BR> </li>
</ul>
  <li> Non-CS electives (9 additional credits):
  advisor-approved electives (excluding CS and PEAC)
  <li>Free Electives (12 additional credits): advisor-approved free electives   (excluding PEAC)
  <li>PEAC (2 credits): See Academic and General Information for exceptions.
  <li>MINOR: Students must complete a <a href="http://www.uvm.edu/academics/catalogue/?Page=alldegrees.php&amp;SM=offeringmenu.html&amp;letter=a&amp;type=minors">University-approved minor</a> (excluding Computer Science); courses used to fulfill other requirements may be used to satisfy minor requirements.</li></ul>
<p>
<strong>Grade Policy for BS(CS)</strong><br />
No grade below a C- in any computer science course will be accepted, except as   free elective credit.</p>
