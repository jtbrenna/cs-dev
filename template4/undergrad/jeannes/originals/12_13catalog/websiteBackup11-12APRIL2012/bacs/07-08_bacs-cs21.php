            <P>Includes Arts and Sciences General and Distribution Requirements,
            and other elective credits.</P>
            <P><EM><STRONG>Notes: </STRONG></EM><BR><STRONG>a)</STRONG> A&amp;S
            General requirements are 1 course each from non-European cultures
            and Race Relations &amp; Ethnicity. For Distribution requirements,
            the Math and the Natural Sciences categories are already complete
            within the requirements for the CS major. Therefore, students need
            to complete <STRONG>four </STRONG>from the following Distribution
            categories: Foreign Language, Fine Arts, Literature, Humanities, and
            Social Sciences.</P>
            <P><STRONG>b)</STRONG>&nbsp;<STRONG>Science Requirement:</STRONG>
            The Natural Science category of the A&amp;S Distribution
            Requirements must be filled. A two-semester laboratory science
            sequence is <EM>recommended</EM>.</P>
            <P><STRONG>c)</STRONG> A <STRONG>Minor </STRONG>is required in this
            degree program. Do not wait too long to begin the Minor courses
            since there may be prerequisite dependencies.</P>
            <P><STRONG>d)</STRONG> CS courses that are not required may be
            considered as elective choices toward degree credits. (CS 2 is
            recommended for the first-year first semester if your CS background
            is weak or nonexistent. CS 14 Visual BASIC, CS 8 Web Design, and
            other CS 2XX courses may also be considered if you have the
            prerequisites.)</P>
            <P><STRONG>e)</STRONG> Depending on your course selections you may
            need 1 or 2 elective credits to meet the minimum. However, you may
            need to take a 3-credit course because 1- or 2-credit courses are
            relatively rare. A 4-credit course (rather than 3) may also give you
            the odd credit.</P>
            <P><STRONG>f)</STRONG> PEAC credits are not shown in this
            program.</P></TD></TR>
