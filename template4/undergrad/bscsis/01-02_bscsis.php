<h4>Requirements</h4>
<p>For the Bachelor of Science, Major in Computer Science and Information Systems (BS[CSIS]), the requirements for Catalogue 2001-02 are a minimum of 128 credits (126 for students exempt from PEAC requirement) as follows:</p>
<ul>
	<li>Business Administration: 60, 61, 120, 132, 141, 143, 144, 150, 173, 180. </li>
	<li>Computer Science: 14, 21, 26, 100, 101, 103, 104, 292, plus three   additional 200-level courses not more than three credits of which may be independent study </li>
	<li>Economics: 11, 12 </li>
	<li>Mathematics: 19, 20, 54 </li>
	<li>One sequence from
	  <ul>
          <li>Biology: 1, 2 </li>
	    <li>Chemistry: 31, 32 </li>
	    <li>Physics: 31 with 21; 42 with 22 </li>
      </ul>
  </li>
	<li>Statistics: 141 </li>
	<li>English 1 </li>
	<li>9 credits (3 courses) of Social Science Electives selected from: ALANA,   Anthropology, Economics, Geography, History, Political Science, Psychology, Sociology, Women's Studies, or other advisor-approved electives. </li>
	<li>9 credits (3 courses) of Humanities and Fine Arts Electives selected from: Art, Classics, Drama, Film, Language, Literature, Music, Philosophy, Religion, Speech, or other advisor-approved electives. </li>
	<li>15 credits in advisor-approved free electives (excluding PEAC) </li>
	<li>AH 95, AGRI 95, or one course approved by the College of Arts and Sciences as a course in non-European Cultures or Race Relations and Ethnicity, as specified under General Requirements in the College of Arts and Sciences section of this catalogue (a course used to fulfill other elective or distribution requirements may be used to fulfill this requirement)&nbsp; </li>
	<li>PEAC: Two credits of Physical Education Activities.</li>
</ul>
<p>
<strong>Grade Policy for BS(CS)</strong><br />
No grade below a C- in any computer science or business administration course will be accepted, except as free elective credit.</p>
<p>This program is intended to fulfil the course requirements to be eligible for advanced standing in the MBA program at UVM.</p>
