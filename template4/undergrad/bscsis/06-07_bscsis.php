<h4>Requirements</h4>
<p>For the Bachelor of Science, Major in Computer Science and Information Systems (BS[CSIS]), the requirements for Catalogue 2006-07 are a minimum of 122 credits (120 for students exempt from PEAC requirement) as follows:</p>
<ul>
	<li>Computer Science (35 credits): 14, 21, 26, 100, 101, 104, 148, 292, plus three additional credits (1 course) at the 100-level or above (CS 103 is recommended for students who wish to pursue graduate study in computer science), plus 9 additional credits (3 courses) at the 200-level not more than 3 credits of which may be independent study; </li>
	<li>Business Administration (29 credits): 60, 61, 120, 132, 141, 143, 150, 173,   180; </li>
	<li>Economics (6 credits): 11, 12 ; </li>
	<li>Mathematics (9-11 credits): 19+20 or 21+22 (recommended), 54 ; </li>
	<li>Statistics (3 credits): Stat 153; </li>
	<li>Science (8-10 credits): 1 laboratory science sequence, selected from the   following 3 sequences:
	  <ul>
          <li>Biology: 1, 2 </li>
	    <li>Chemistry: 31, 32 </li>
	    <li>Physics: 31 with 21; 42 with 22 </li>
      </ul>
  </li>
	<li>Writing (3 credits): English 1, 50, or 53</li>
	<li>HSS(18 credits): 6 courses in Social Science, Humanities, and Fine Arts   electives. At least 6 credits must be chosen from the Social Science Group and at least 6 credits must be chosen from the Humanities and Fine Arts Group as defined below. In addition, students must either complete a 1 credit Race &amp; Culture course or ensure that at least one HSS elective also satisfies the <a href="http://www.uvm.edu/academics/catalogue/?Page=read.php&amp;p=/Colleges_and_Schools/College_of_Arts_and_Sciences/Requirements/College_of_Arts_and_Sciences_Requirements_for_the_Bachelor_of_Arts&amp;SM=collegemenu.html">non-European Cultures or Race Relations &amp; Ethnicity Requirements</a>, as specified under the General Requirements in the College of Arts and Sciences section of the University catalog for this year.<br />
        <ul>
          <li>Social Science Group: ALANA, Anthropology, Economics, Geography, History,   Political Science, Psychology, Sociology, Women's Studies, or other   advisor-approved social science electives. </li>
          <li>Humanities and Fine Arts Group: Art, Classics, Drama, Film, Language,   Literature, Music, Philosophy, Religion, Speech or other advisor-approved humanities or fine arts electives.<br />
          </li>
        </ul>
  </li>
	<li>Free electives to reach the minimum of 120 academic credits (9 credits, no PEAC) ; </li>
	<li>PEAC (2 credits): See Academic and General Information for exceptions.</li>
</ul>
<p>
<strong>Grade Policy for BS(CS)</strong><br />
No more than three grades of D+, D, or D- in CS courses numbered 103 and higher and in BSAD courses numbered 100 and higher.</p>
