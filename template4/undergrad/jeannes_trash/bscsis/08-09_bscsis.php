<h4>Requirements</h4>
<p>For the Bachelor of Science, Major in Computer Science and Information Systems (BS[CSIS]), the requirements for Catalogue 2008-09 are a minimum of 122 credits (120 for students exempt from PEAC requirement) as follows:</p>
<ul>
	<li>Computer Science (38 credits): 14, 21, 64, 110, 121,
  124, 148, 292, plus&nbsp;15 additional credits (5 courses) including 3 credits
  (1 course) at the 100-level or above (CS 123 is recommended for students who
  wish to pursue graduate study in computer science) and 9 credits( 3 courses)
  at the 200-level of which not more than 3 credits may be independent
  study;
  <BR>[The remaining 3 credits of the "15 additional credits", may be at the 0xx level or above.
  Students with no programming experience should take a course with a programming
  component at the 0xx level *prior* to CS21 Computer Programming I.]

	<li>Business Administration (27 credits): 60, 61, 120,
  132, 141, 143, 150, 173, 180;

	<li>Economics (6 credits): 11, 12 ;

	<li>Mathematics (6-8 credits): 19+20 or 21+22
  (recommended) ;

	<li style="COLOR: brown">Statistics (3 credits): Stat 153
  [CSIS majors should&nbsp;replace STAT 153&nbsp;with STAT 141&nbsp;to meet
  prerequisites for BSAD courses.&nbsp; This exception was approved by CS Curriculum Committee 3/6/08.]&nbsp;;

	<li>Science (8-10 credits): 1 laboratory science sequence, selected from the   following 3 sequences:
	  <ul>
          <li>Biology: 1, 2
	    <li>Chemistry: 31, 32
	    <li>Physics: 31 with 21; 42 with 22 </li>
      </ul>
	<li>Writing (3 credits): English 1, 50, or 53

	<li>HSS(18 credits): 6 courses in Social Science,
  Humanities, and Fine Arts electives. At least 6 credits must be chosen from
  the Social Science Group and at least 6 credits must be chosen from the
  Humanities and Fine Arts Group as defined below.
        <ul>
          <li>Social Science Group: ALANA, Anthropology,
    Economics, Geography, History, Political Science, Psychology, Sociology,
    Women's Studies, or other advisor-approved social science electives.
          <li>Humanities and Fine Arts Group: Art, Classics,
    Drama, Film, Language, Literature, Music, Philosophy, Religion, Speech or
    other advisor-approved humanities or fine arts electives.<BR><BR>Credits used to fulfill the <STRONG>UVM general diversity
requirement</STRONG> can be applied
  towards these HSS requirements.&nbsp;&nbsp;[For the&nbsp;University general
  diversity requirement see courses specified by the university
  catalogue:&nbsp;&nbsp;
  <A href="http://www.uvm.edu/academics/catalogue2008-09/?Page=allpolicies.php&SM=policymenu.html&policy=Degree%20Requirements%20for%20Undergraduates"
   >General Requirements</A>.]
          </li>
        </ul>
	<li>[Free electives (11 credits, no PEAC)&nbsp;to reach
  the minimum of 120 academic credits]

	<li>PEAC (2 credits): See Academic and General Information for exceptions.</li>
</ul>
<p>
<strong>Grade Policy for BS(CSIS)</strong><br >
No more than three grades of D+, D, or D- in CS courses numbered 123 and higher and in BSAD courses numbered 100 and higher.</p>
