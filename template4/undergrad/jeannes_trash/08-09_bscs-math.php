</p>
<p>
d) <strong>Minor:</strong> There may be prerequisite sequences, so don’t wait too long to begin.
</p>
<p>
e) <strong>Science requirement:</strong> Four courses chosen from: Biology 1, 2, Chemistry 31, 32, Physics 31 with 21, 42 with 22.</p>
<p>
Chemistry 1 or Biology 1 is typically chosen as the first Science Elective since MATH 21 is a prerequisite for Physics 31/21. Biology 2 may actually be taken prior to Biology I.
</p>
<p>
   Physics: The CS Department recommends Physics 31/21 and 42/22 as Science Electives due to its relevance in the computing field. Physics is needed for some Minors (e.g., EE). It is not, however, an absolute requirement.&nbsp;
</p>
<p>
f) <strong>Odd Credits:</strong> Depending on your course selections you may need 1 or 2 elective credits to meet the minimum. However, you may need to take a 3-credit course because 1- or 2-credit courses are relatively rare. A 4-credit course (rather than 3) may also give you the odd credit.
