<h4>Requirements</h4>
<p>For the Bachelor of Science, Major in Computer Science (BS[CS]), the requirements for Catalogue 2008-09 are a minimum of 122 credits   (120 for students exempt from PEAC requirement) as follows:</p>
<UL>
	<li> Computer Science (44 credits): 21, 64, 110, 121, 123,
  124, 201, 224 or 243, 292, and 18 additional credits (6 courses), including 15
  credits at the 200-level (not more than three credits of which may be
  independent study).
  <BR>[The remaining 3 credits of the 18 additional credits
  may be at the 0XX level or above. Students with no programming
  experience should take one 3-credit course at the 0XX level with a
  programming component prior to CS 21 Computer Programming I.
  See <A href="../../?Page=undergrad/bscs/08-09_bscs.php&amp;SM=undergrad/_undergradmenu.html#sequence"
 >Course
  Sequence</A>.]
	<li> Mathematics (14 credits): 21, 22, two of (121, 124, 173, 271)
	<li>        Statistics (3 credits): 153
    <li>Science (13 credits): advisor-approved science electives in Astronomy, Anatomy
  and Neurobiology, Biology, Botany, Chemistry, Environmental Science,
  Geology, Microbiology and Molecular Genetics, Nutrition and Food
  Sciences, Physics, or Electrical Engineering 3 or 4, to include 1 laboratory
  science sequence selected from the following:<BR>
  <ul>
		<li> Biology: 1 or 11, 2 or 12
		<li> Chemistry: 31 or 35, 32 or 36
	  <li>Physics: 31 with 21, 42 with 22 <BR><strong>Note:</strong> Certain minors
    require specific science courses.</li>
  </ul>
  <li>Technical Electives (6 credits): Two courses in
  approved <a href="../../?Page=undergrad/bscs/08-09_bscs.php&amp;SM=undergrad/_undergradmenu.html#techelectives">technical electives</a>.
  <li> Writing (3 credits): English 1, 50, or 53
  <LI>Humanities and Social Science (HSS) (18 credits): 6 courses in Social Science, Humanities, and Fine Arts
  electives. At least 6 credits must be chosen from the Social Science Group and
  at least 6 credits must be chosen from the Humanities and Fine Arts Group as
  defined below. <BR>
  <UL>
    <li>Social Science Group: ALANA, Anthropology,
    Economics, Geography, History, Political Science, Psychology, Sociology,
    Women's Studies, or other advisor-approved social science electives.
    <li>Humanities and Fine Arts Group: Art, Classics,
    Drama, Film, Language, Literature, Music, Philosophy, Religion, Speech or
    other advisor-approved humanities or fine arts electives. </li></UL></LI></UL>
<BLOCKQUOTE dir=ltr style="MARGIN-RIGHT: 0px">
<P>Credits used to fulfill the <STRONG>UVM general diversity
requirement</STRONG> can be applied
  towards these HSS requirements.&nbsp;&nbsp;[For the&nbsp;University general
  diversity requirement see courses specified by the university
  catalogue:&nbsp;&nbsp;
  <A href="http://www.uvm.edu/academics/catalogue2007-08/?Page=allpolicies.php&SM=policymenu.html&policy=Degree%20Requirements%20for%20Undergraduates"
 >[old 07-08]General Requirements</A>.] </P></BLOCKQUOTE>
<UL>
  <LI>Non-CS electives (9 additional credits):
  advisor-approved electives (excluding CS and PEAC)

  <li> Free Electives (to reach the minimum of 120 credits,
    no PEAC) [10 credits]
  <li>PEAC (2 credits): See Academic and General Information for exceptions.
  <li>MINOR: Students must complete a <a href="http://www.uvm.edu/academics/catalogue/?Page=alldegrees.php&amp;SM=offeringmenu.html&amp;letter=a&amp;type=minors">University-approved minor</a> (excluding Computer Science); courses used to fulfill other requirements may be used to satisfy minor requirements.</li></UL>
<p>
<strong>Grade Policy for BS[CS]</strong><br >
No more than three grades of D+, D, or D- in CS courses numbered 123 and higher.
</p>
