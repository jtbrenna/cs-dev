<p>In addition, students must complete the following required courses:
</p>
<ul>
  <li><strong>Natural Science Distribution Electives</strong> (2 courses, at least one with lab; <em>typically satisfied in 1st or 2nd year</em>)
  <li><strong>HSS Distribution Electives </strong>(satisfy distribution categories: foreign language, fine arts, literature, humanities,   social sciences; <em>typically satisfied in 1st through 3rd years</em>)
  <li><strong>General Requirements </strong>(non-European cultures and Race Relations &amp; Ethnicity; some courses also fulfill HSS   Distribution requirements; <em>typically satisfied in 1st or 2nd years</em>)
  <li><strong>Approved Minor </strong>(usually ~6 courses; some courses also   fulfill HSS Distribution requirements; be careful not to start your minor too   late, as there may be prerequisite dependencies in courses required for the   minor; <em>typically taken in 2nd through 4th years</em>)
  <li><strong>Physical Education </strong>Activities (2 credits, unless exempt)
  <li><strong>Free Electives </strong>(to complete the 122 credit requirement;   note restrictions on non A&amp;S credits) </li>
</ul>
<p>We recommend 14-17 credits per semester, in order to graduate in 8 semesters.</p>

