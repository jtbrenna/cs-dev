<h4>Is "getting a job" the only reason to study Computer Science? </h4>
<p>
Of course, it's a very good reason. But we don't think it's the only reason by any means. Computer Science is a broad discipline. It has strong connections with business, mathematics, the sciences, engineering, entertainment, the fine arts, education, and on and on. It's a creative subject area in which problem solving and invention are the main ingredients. All of these make Computer Science an excellent background for a variety of occupations. It's fun to create enrivonments that may exist only in our imaginations.</p>

