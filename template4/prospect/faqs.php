<h4>What is unique about Computer Science at UVM? </h4>
<p>
Computer Science is much more than just programming. Just as good handwriting and typing skills don't make a person capable of producing literature, knowing how to program, in and of itself, doesn't make a person able to conceive and write literate programs that solve difficult problems.</p>
<p>An understanding of what computers are fundamentally incapable of doing, how they can perform tasks normally thought to require human intelligence, and how they can store, manipulate, and transmit massive amounts of information quickly and accurately, is needed to be able to design computer-based solutions to real-world problems. Many places can teach you to program, but our goal is to help you develop a more fundamental knowledge so that you can be a creative problem solver, as well as a literate programmer.</p>
<p>Most four-year Computer Science programs have this objective, so what does UVM offer that makes it unique?</p>
<ul>
<li><strong>Small classes with plenty of opportunity to interact with the instructors.</strong> Our courses are taught by faculty instructors only (unless there are exceptional cases to make), and the faculty instructors go beyond their duties to help students.</li>
<li><strong>Good lab facilities</strong> with undergraduate teaching assistants, graduate teaching fellows, and instructors available for individual help. </li>
<li><strong>A very competitive curriculum</strong>, with excellent coverage of both Windows and Unix, and cutting-edge senior courses in areas such as bioinformatics, cryptography, data mining, evolutionary &amp; agent-based computing, graphics, networks, and parallel processing. </li>
<li><strong>Research opportunities with an award-winning faculty</strong> who is involved in the forefront of research in knowledge and data engineering, software engineering and verification, computational sciences, and computer security.</li>
<li><strong>Real-world projects</strong> (with possible course credits) <strong>and industrial scholarships</strong> provided by the Vermont Information Technology Partnership (VITP) to enhance academic-industry interaction. </li>
</ul>

