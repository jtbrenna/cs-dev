<h4>How do you prepare for a career in Computer Science? </h4>
<p>
In high school, it's best to take four years of mathematics, including precalculus. Three years of science should be taken, including courses in chemistry and physics. Courses that include writing are strongly encouraged. Computer programming courses can give you a head start on your college work.</p>
<p>In college you will take several courses in mathematics as well as courses in the sciences. You'll take introductory courses in computer programming, then you'll apply all this knowledge to a variety of CS courses, both theoretical and practical. The theoretical courses provide a concrete understanding of the principles behind computers and computing. The more practical courses involve a significant amount of computer programming and are intended to make you an excellent programmer by the time you graduate.</p>

