<h4>How is the job situation in the field of Computer Science? </h4>
<p>
Our students are well placed upon graduation, and indeed many find interesting <a href=http://www.uvm.edu/~cems/?Page=internship/internships.html&SM=internship/_internshipmenu.html>internships</a> and co-ops during their junior and senior years.  Employers contact the department on a regular basis seeking to hire UVM students in Computer Science. In fact, <a href="../../?Page=current/careerservices.php&amp;SM=current/_currentmenu.html">Career Services</a> often experiences the happy difficulty of having more employers coming to campus looking for CS majors than there are students in the program!</p>
<p>
For more information on the state of the field, please see the <a href="http://computingcareers.acm.org/?page_id=4">Top 10 Reasons to Major in Computing</a> compiled by the Association for Computing Machinery (ACM).</p>

