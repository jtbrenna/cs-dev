<h3>Seminar Description</h3>

<h4 class="subtitle">Biodiversity Informatics: Developing Computational
Approaches to Link Knowledge Across the Spectrum of Life</h4>

<p><a href="http://www.uvm.edu/~insarkar/"><strong>Dr. Neil Sarkar</strong></a><br>
Director of Biomedical Informatics at Center for Clinical and Translational Science, and Assistant Professor, Department of Microbiology and Molecular Genetics, College of Medicine, University of Vermont.
</p>

<p><strong>April 27, 2009</strong><br>
12:50 - 1:45 pm<br>
207 Votey Hall</p>

<p><strong>Summary</strong>
<br>
Biodiversity informatics is an emerging discipline that strives to 
integrate biological information from a range of contemporary and historical 
sources across the spectrum of life using organisms as the linking thread. 
This presentation will begin with an introduction to the challenges of developing a 
&quot;taxonomic intelligent&quot; framework for organizing biological information. The identification of organism information will then be discussed within two contexts: (1) from literature using named entity recognition methods; and (2) from molecular data using phylogenetic techniques. The presentation will conclude with an exploration of methods to integrate relevant biodiversity information within the context of infectious disease.</p>
<p><strong>Speaker Bio</strong>
<br>
Indra Neil Sarkar, PhD, MLIS is Director of Biomedical Informatics within the Center for Clinical and Translational Science at the University of Vermont College of Medicine. He is also an Assistant Professor in the Department of Microbiology and Molecular Genetics. His research involves the development and use of a range of computational techniques (including novel knowledge gathering and discovery methods, phylogenetics, information theory, and natural language processing) in the study of the evolutionary history of infectious diseases and the biology of aging. Much of his research focuses on the integration of disparate knowledge sources through the use of ontology-based information retrieval methods and indexing technologies. Dr. Sarkar's work ultimately aims to integrate biomedical and biodiversity knowledge to facilitate the generation of comparative biology hypotheses that can be ultimately tested within a translational science framework. Previously, Dr. Sarkar held scientific appointments at the Marine Biological Laboratory and the American Museum of Natural History. His current research is funded by the National Institutes of Health and the National Science Foundation.<p>

