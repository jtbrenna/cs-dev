<h3>Seminar Description</h3>

<h4 class="subtitle">Biologically Inspired Motor Control for Underactuated Robots</h4>

<p><a href="http://people.csail.mit.edu/iida/"><strong>Dr. Fumiya Iida</strong></a><br>
Robot Locomotion Group,
Computer Science and Artificial Intelligence Laboratory, 
Massachusetts Institute of Technology
</p>

<p><strong>March 30, 2009</strong><br>
12:50 - 1:45 pm<br>
207 Votey Hall</p>

<p><strong>Summary</strong>
<br>
If compared with biological systems that routinely exhibit dynamic behaviors in complex
environment with surprising adaptivity, energy efficiency and robustness, our robots are
still severely suffering from the lack of sensory-motor and learning capabilities. To account 
for the discrepancy of behavior control in animals and robots, the primary goal of our research 
projects is to develop technological means to control underactuated robotic systems for 
rapid, efficient and maneuverable behaviors in the real world. In this talk, I will introduce 
three key technologies bridging over computer science and mechanical engineering that 
we have been investigating in the context of dynamic robot locomotion. First, 
I will introduce our exploration of theoretical models to obtain a comprehensive 
understanding of mechanical self-stability. As observed in nature, animals actively 
utilize the mechanical properties such as passive dynamics and viscous-elastic 
muscle-tendon systems for regulating their behaviors in dynamic environments. 
We will introduce how such dynamic mechanical interactions can be exploited in 
robot locomotion, in which the dynamics plays a role of &quot;mechanical feedback&quot; by 
physically compensating against disturbances without explicit sensing and control. Second, on the basis of the theoretical investigations of self-stability, we will discuss design optimization techniques to enrich behavioral diversity. Here I will demonstrate a number of dynamic locomotion robots exhibiting walking, running, hopping, swimming and dancing. And third, we will discuss how underactuated robotic systems can be more precisely controlled by employing computational optimization techniques such as reinforcement learning. While control of underactuated robots are generally regarded as a hard problem due to the nonlinear dynamics, here we demonstrate how self-stability and mechanical dynamics can be exploited in the control optimization of for resilient behavior patterns such as high-jumps over large obstacles and a biped robot traveling through rough terrains.
</p>
<p><strong>Speaker Bio</strong>
<br>
Fumiya Iida received his bachelor and master degrees in mechanical engineering at Tokyo University of Science (Japan), and Ph.D. at the Faculty of Science, University of Zurich (Switzerland, 2006). From 2004 to 2005, he was also engaged in biomechanics research of human locomotion at Locomotion Laboratory, University of Jena (Germany). He is currently a postdoctoral associate at the Computer Science and Artificial Intelligence Laboratory, Massachusetts Institute of Technology. He has been involved in a number of research projects related to robotics, embodied artificial intelligence and biomechanics, and his research interest includes biologically inspired robotics, navigation of autonomous robots, and dynamic legged locomotion. He serves as the editorial board of a few journals and the program committee member of a dozen of international conferences, and he was a recipient of the Prospective Researcher Fellowship of the Swiss National Science Foundation.</p>
