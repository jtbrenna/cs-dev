<h3>Seminar Description</h3>

<p><h4 class="subtitle">&quot;Design Tools for Sensor-Based Science&quot;</h4></p>

<p><a href="http://www.cs.jhu.edu/~terzis/">Andreas Terzis</a><br />
Department of Computer Science, Johns Hopkins University</p>

<br />

<p>
<strong>  April 6, 2007</strong><br />
12:20 - 1:10 pm<br />
Votey Hall, Rm. 367  </p>

<p><strong>Summary</strong>
<br />
Andreas will describe the architecture of software tools that aid scientists in designing and programming wireless sensor networks (WSNs). The goals of his system are (1) to reduce the complexity of deployment by automatically programming a sensor network and database based on a visual description of a site and experiment and (2) to provide a simple, intuitive network design tool that explores trade-offs in cost, reliability, and network lifetime. He will discuss the need for such tools based on his experience in deploying a sensor network used to study soil ecology. He will also present techniques for the development of site-specific transmission models, the placement of motes and gateways, and data processing in hierarchical networks.</p>

<p><strong>Speaker Bio</strong>
<br />
Andreas Terzis is an Assistant Professor in the Department of Computer Science at Johns Hopkins University. He joined the faculty in January 2003. Before going to JHU, Andreas received his Ph.D. in computer science from UCLA in 2000. Andreas heads the Hopkins InterNetworking Research (HiNRG) Group where he conducts research in wireless sensor networks and network security.</p>

