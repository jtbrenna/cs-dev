<h3>Seminar Description</h3>

<p><h4 class="subtitle">&quot;Theory and Strategy of Coding in Microbe Genomes: Case Studies in Informational Evolutionary Theory&quot;</h4></p>

<p>
<a href="http://www.santafe.edu/~krakauer"><strong>David C. Krakauer</strong></a><br />
Santa Fe Institute</p>

<p>
<strong>  February 12, 2007</strong> <br />
12:20 - 1:10 pm<br />
Billings North Lounge</p>

<p><strong>Summary</strong>
<br />
Darwin's theory of evolution was developed without reference to the microscopic world or the genetic underpinnings of variation. Modern evolutionary theory has benefited from the fact that microbes evolve over the course of days and years permitting an exact description of changing genetic structure. Unlike Darwinian evolution that stresses morphological properties, modern evolution stresses informational properties and their relation to form. I shall present four concepts which I argue will be central to an extended, twenty first century evolutionary theory: robustness, evolveability, redundancy and compression. These are also key concepts in engineering and computer science. The application of these concepts will be explored in relation to the structure and function of microbial genomes.</p>
