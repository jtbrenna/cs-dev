<p><strong>Summary</strong>
<br />
A primary goal of computer graphics is to generate images indistinguishable from those taken in the real world. Achieving such realism requires an accurate simulation of the physical interactions of light with the scene. Images of this quality are now prevalent in computer-generated movies, but require hours of computation for each frame of the movie. Many applications, however &#151; such as flight simulators and video games &#151; are interactive and must generate tens of images every second.
</p>
<p>
Photon mapping is a popular algorithm that is able to support many realistic lighting effects such as indirect illumination. Although it is more efficient than the comparable alternatives, it does require significantly more memory bandwidth than is feasible today in interactive systems.
</p>
<p>
After an introduction to computer graphics, global illumination and photon mapping, we'll examine three novel techniques, one in depth, that make the interactive generation of images using a photon map feasible. These techniques are integrated into a hardware architecture. This talk has been designed to be accessible to those with a general background in computer science &#151; knowledge of computer graphics is not assumed.
</p>

<p><strong>Speaker Bio</strong>
<br />
Dr. Steinhurst is an Assistant Professor of Computer Science at Bucknell University. He conducts research in the field of computer graphics, specializing on the fast and accurate simulation of light transport using specialized hardware. This has applications in the entertainment industry such as video games and movies, industrial training such as virtual product prototyping, and in the study of light and color phenomena in nature, such rainbows and atmospheric halos. His teaching interests include core courses, computer organization, architecture, graphics, high performance computing and the support of computational thinking across both engineering and the liberal arts curriculums.
</p>
<p>
Professor Steinhurst received his PhD and MS from the University of North Carolina at Chapel Hill, funded by numerous fellowships including the National Science Foundation Graduate Research Fellowship. In 1999 he graduated with a BS in Computer Science with a minor in mathematics from the University of Vermont. While a student at UVM, he co-founded the Computer Science Student Association (CSSA) and served as its first president.
</p>

