Dept. of Computer Science and Engineering<br />
Wright State University
</p>

<p><strong>October 8, 2007</strong> <br />
12:20 - 1:10 pm<br />
Votey Hall, Rm. 367</p>  
<p><strong>Summary</strong>
<br />
Discovering contrasts between collections of data is an important task in data mining. In this talk, we consider a new type of contrast pattern called a "minimal distinguishing subsequence" or MDS. An MDS is a minimal subsequence that occurs frequently in one class of sequences and infrequently in sequences of another class. It is a natural way of  representing strong and succinct contrast information between two sequential datasets and can be useful in applications such as protein comparison, document comparison and building sequential classification models.
</p>
<p>
Mining MDS patterns is a challenging task and is significantly different from mining contrasts between relational/transactional data. One particularly important type of constraint that can be integrated into the mining process is the gap constraint. We present an efficient algorithm called ConSGapMiner (Contrast Sequences with Gap Miner), to mine all MDSs satisfying a minimum and maximum gap constraint, plus a maximum length constraint. It employs highly efficient bitset and boolean operations, for powerful gap-based pruning within a prefix growth framework. A performance evaluation with both  sparse and dense datasets, demonstrates the scalability of ConSGapMiner and shows its ability to mine patterns from high dimensional datasets at low supports.
</p>
<p>
This talk is based on a paper that received the 2005 IEEE ICDM Best Paper Award and was co-authored with Xiaonan Ji and James Bailey of University of Melbourne.
</p>

<p><strong>Speaker Bio</strong>
<br />
Guozhu Dong is a professor at Wright State University. He received his PhD from the University of Southern California in 1988. His main research interests are databases, data mining, and bioinformatics. He has published over 100 journal/conference articles, published a book entitled <em>Sequence Data Mining</em>, holds 3 US patents, and won the Best Paper Award from IEEE ICDM in 2005.

He was a program committee co-chair of the 2003 International Conference on Web-Age Information Management (WAIM) and of the 2007 Joint International APWeb/WAIM Conferences. He serves on the steering committee of the WAIM conference series and the editorial board of the International Journal of Information Technology. He has served on over 50 conference program committees (e.g., IEEE ICDE, IEEE ICDM, ICDT, ACM PODS, ACM SIGKDD, VLDB). His research has been funded by government agencies (NSF, ARC, AFRL) and private corporations. He is a senior member of IEEE and a member of ACM.

Representative previous research includes: first-order incremental view maintenance for transitive closure and shortest path views; expressive power of first-order queries with arithmetic constraints; the mining of emerging/contrast patterns and the use of such patterns for classification and bioinformatics; and various OLAP/OLAM cubing (iceberg, gradient, regression, and bound-prune) algorithms.
</p>

