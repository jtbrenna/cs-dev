<h3>Seminar Description</h3>

<p><h4 class="subtitle">&quot;What Has Evolved? Sorting Through the Debris of Evolution&quot;</h4>
</p>

<p><strong><a href="http://eldar.mathstat.uoguelph.ca/dashlock/">Daniel Ashlock</a></strong><br />
Dept. of Mathematics and Statistics, University of Guelph</p>

<strong>Sept. 5, 2007</strong> <br />
12:20 - 1:10 pm<br />
Votey Hall, Rm. 367  
<p><strong>Summary</strong>
<br />
Prisoner's dilemma is a widely used game-theoretic model of cooperation and conflict. Evolutionary algorithms are able to rapidly train large number of agents to play prisoner's dilemma. This talk introduces the exploration of prisoner's dilemma with evolutionary algorithms. The central problem treated in the talk is trying to understand what is happening in the evolving populations of prisoner's dilemma agents. A tool, called fingerprinting, is introduced to automate analysis. Evolution is found to locate agents with general skill at the prisoner's dilemma, even when matched with agents with which they share no evolutionary history. This phenomena is called non-local adaptation. New results of the impact of noise on non-local adaptation and the character of evolving populations are presented. The talk assumes no familiarity with evolutionary computation or game theory.</p>

<p><strong>Speaker Bio</strong>
<br />
Dr. Daniel Ashlock holds a chair in bioinformatics at the University of Guelph where he is a professor of mathematics.  His research includes a diverse set of topics including genetic mapping of maize, machine learning to improve the design of PCR primers, design of efficient wood burning stoves for use in the third world, applications of evolutionary computation to ecological data, evolved art, and the representation problem in evolutionary computation.  He is currently helping to develop a graduate program in bioinformatics at Guelph.</p>
