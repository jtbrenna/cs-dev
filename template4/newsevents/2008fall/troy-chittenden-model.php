<h3>Seminar Description</h3>

<h4 class="subtitle">"Implementing an Integrated Land Use, Transportation, and Environmental Quality Simulation Model for Chittenden County"</h4>

<p><a href="http://www.uvm.edu/%7Eatroy/"><strong>Dr. Austin Troy</strong></a><br />
Rubenstein School of Environment and Natural Resources<br>
and Department of Computer Science<br>
University of Vermont</p>

<p><strong>October 6, 2008</strong><br>
12:20 - 1:15 pm<br>
367 Votey Hall</p>

<p><strong>Summary</strong>
<br>
This presentation discusses the development and implementation of an
integrated land use, transportation, and environmental quality
simulation modeling framework for Chittenden County, VT. This modeling
framework allows planners and other stakeholders to simulate land use
change, residential and commercial sector dynamics, traffic patterns,
and environmental impacts associated with Chittenden County's projected
population growth. It also allows alternative scenarios to be compared
against baseline conditions to help evaluate the potential consequences
of different policy and investment scenarios (e.g. construction of new
highway infrastructure or changes to zoning). The framework integrates
the UrbanSim open source land use simulation model and the TransCAD
4-step travel demand model, and is attempting to further integrate
TRANSIMS, a open-source microscopic traffic simulation model. We are
also in the process of developing new utilities that calculate
spatially-explicit environmental indicators (e.g. water quality,
habitat fragmentation, carbon footprint, energy usage) from the
standard outputs of UrbanSim and TransCAD. After 5 years of data
development and processing, UrbanSim and TransCAD have been
successfully implemented for the County for a 1990 base year. This
model is in the process of being validated against data from the year
2005. Several alternative scenarios have also been run.</p>

<p><strong>Speaker Bio</strong>
<br>
Dr. Troy is Associate Professor at the Rubenstein School of
Environment and Natural Resources at the University of Vermont with a
secondary appointment in Computer Science. He is also a fellow of the
Gund Institute of Ecological Economics and a Co-Principal Investigator
on the Baltimore Ecosystem Study, which is a long-term ecological
research (LTER) project of the National Science Foundation. He
teaches courses in Geographic Information Science (GIS), spatial
statistics, and land use policy and economics. His research
looks at urban and suburban environmental management issues, using
spatial analysis methods, including GIS, remote sensing, spatial
econometrics, and simulation modeling. He currently has research
funding through the US Department of Transportation, the UVM
Transportation Research Center, the USDA Forest Service, the National
Science Foundation, and the Northeastern States Research
Cooperative. He has a bachelor's degree from Yale, a Master of
Forestry degree from Yale School of Forestry and Environmental Studies
and a PhD from the department of Environmental Science, Policy &amp;
Management at U.C. Berkeley.
</p>

