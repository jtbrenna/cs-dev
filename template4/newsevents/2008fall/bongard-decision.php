Dept. of Computer Science, University of Vermont</p>

<p><strong>September 22, 2008</strong><br />
12:20 - 1:15 pm<br />
369 Votey Hall</p>

<p><strong>Summary</strong>
<br />
Internet-based science holds the promise of better quantifying the study of human behavior through systematic interrogation of large, diverse populations. Such studies to date however have focused on analysis after the experiment concluded, or have used a finite set of stimuli. In this talk I will describe a new method for conducting human behavior experiments that can only be done using a computer: subjects are modeled individually as they take the experiment, and stimuli are created for them on the fly to uncover their models' weaknesses. I will show how this approach allows for better modeling &#151; and therefore better understanding &#151; of human decision making.</p>

