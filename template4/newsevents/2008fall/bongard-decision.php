<h3>Seminar Description</h3>

<h4 class="subtitle">&quot;Individualized Modeling of Human Decision Making&quot;</h4>

<p><a href="http://www.cs.uvm.edu/~jbongard/index.html"><img src="http://www.uvm.edu/~cems/cs/gfx/bongard.jpg" alt="Dr. Josh Bongard" width="100" height="116" border="0" class="seminars-rt" /></a>
