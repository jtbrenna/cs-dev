<h3>Seminar Description</h3>

<h4 class="subtitle">"An Introduction to Support Vector Machines, Kernel Methods and Learning Theory"</h4>

<p><strong><a href="http://www.cs.uvm.edu/%7Eapecheni/">Alison Pechenick</a></strong><br>
Dept. of Computer Science, University of Vermont</p>

<p><strong>November 17, 2008</strong><br>
12:20 - 1:15 pm<br>
367 Votey Hall</p>

<p><strong>Summary</strong>
<br>
Since earning his Ph.D. in statistics, Vladimir Vapnik has spent
decades working as a computer scientist in Moscow and the U.S.
Renowned for his seminal work in computational learning theory, Vapnik
also proposed a mathematically elegant, optimal hyperplane algorithm
for classifying linearly-separable data. In the decades that
followed, with the assistance of colleagues such as Bernhard Boser,
Corrina Cortes and Isabelle Guyon, and the inclusion of kernel methods,
Vapnik&#8217;s Support Vector Machine (SVM) has evolved to perform
regression, novelty detection, and most dramatically, the
classification of linearly-inseparable data. As a result, the SVM
is currently one of the most popular and effective tools in the machine
learning arsenal.
</p>
<p>
In this talk, a first introduction to SVM concepts and mathematics will
be presented. Topics include the underlying mathematics
(quadratic programming, dual form, feature space transformations) which
make this technique so attractive and computationally efficient, and
application examples for context.
</p>
<p>
This talk will be of wide interest, mathematics accessible to
upper-level undergraduates, and will include recommendations for
further reading and experimentation.
</p>

<p><strong>Speaker Bio</strong>
<br>
Alison Pechenick is a senior lecturer in Computer Science, and a Ph.D.
student in Environmental Engineering, with a particular interest in
statistical learning tools and their applications. She has just
completed a sabbatical project investigating the effective use of SVMs
to classify crops via their hyperspectral data.
</p>

