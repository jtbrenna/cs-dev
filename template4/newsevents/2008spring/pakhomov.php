<p>
<strong>April 23, 2008</strong><br />
12:20 - 1:10 pm<br />
367 Votey Hall</p>

<p><strong>Summary</strong>
<br />
Annual foot examinations constitute a critical component of care for diabetes. Documented evidence of FE is central to quality of care reporting; however, manual abstraction of electronic medical records (EMR) is slow, expensive and subject to error. Natural Language Processing and Machine Learning are computational techniques frequently used to extract useful information from the unstructured text of clinical reports. We search the text of in- and out-patient Mayo Clinic physician reports with queries composed of key terms to identify the evidence of foot examinations. Subsequently, we apply a set of automatic classifiers based on the Support Vector Machine learning to determine the structural, vascular and neurological findings of the examination. System and experimental design will be described and advantages and limitations of this methodology will be discussed.
</p>

<p><strong>Speaker Bio</strong>
<br />
Dr. Pakhomov's primary research interest is in computational methods for structuring unstructured data with a focus on extracting patient specific information from speech and free text of medical reports and biomedical articles. Dr. Pakhomov earned a Doctorate degree in Linguistics with a Cognitive Science emphasis from the University of Minnesota, Twin Cities in 2001. Since 2005, Dr. Pakhomov has been engaged in translational research under the NIH Roadmap Multidisciplinary Clinical Research Career Development Award. He currently holds an Assistant Professor position at the University of Minnesota's College of Pharmacy with research appointments in the Institute for Health Informatics and the Mayo Clinic. One of his current projects investigates the application of Natural Language Processing (NLP) and Machine Learning (ML) methods to identification of symptoms and patients with syndromic conditions such as heart failure, angina pectoris and rheumatoid arthritis based on the free text of clinical reports. Dr. Pakhomov is also pursuing applications of NLP and ML to identification of potential risk factors and outcomes (e.g., medication exposure, physical examination findings, functional status) from electronic medical records and biomedical literature. These efforts are aimed at facilitating translational research by providing reliable patient phenotype information, and improving quality of care and patient safety by delivering actionable patient level information at the point of care.
</p>

