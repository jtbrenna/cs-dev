<h3>Seminar Description</h3>

<h4 class="subtitle">&quot;Top-10 Algorithms in Data Mining&quot;</h4>
<p></p>
<strong><a href="http://www.cs.uvm.edu/~xwu/home.html">Dr. Xindong Wu</a></strong><br />
Department of Computer Science, University of Vermont
<br />

<p>
<strong>March 21, 2008</strong><br />
12:20 - 1:10 pm<br />
367 Votey Hall</p>

<p><strong>Summary</strong>
<br />
This talk will present the following top-10 data mining algorithms identified by the IEEE International Conference on Data Mining (ICDM): C4.5, K-Means, SVM, Apriori, EM, PageRank, AdaBoost, kNN, Naive Bayes, and CART. For each algorithm, we provide a description of the algorithm and discuss its impact, as well as current and further research on the algorithm.
</p>

<p><strong>Speaker Bio</strong>
<br />
Dr. Xindong Wu is a professor and the chair of the Department of Computer Science at the University of Vermont. Dr. Wu holds a PhD in Artificial Intelligence from the University of Edinburgh, Britain. His research interests include data mining, knowledge-based systems, and Web information exploration. He has published over 160 refereed papers in these areas in various journals and conferences, including IEEE TKDE, TPAMI, ACM TOIS, DMKD, KAIS, IJCAI, AAAI, ICML, KDD, ICDM, and WWW, as well as 18 books and conference proceedings. In addition, Dr. Wu won the 2005 IEEE ICTAI Best Paper Award and the 2007 IEEE ICDM Best Theory/Algorithms Paper Runner Up Award. For more details about his work, <a href="http://www.cs.uvm.edu/~xwu/home.html">visit Dr. Wu's  website</a>.
</p>

