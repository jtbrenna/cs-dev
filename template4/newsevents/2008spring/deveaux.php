<p><strong>Summary</strong>
<br />
Data mining has been defined as a process that uses a variety of data analysis and modeling techniques to discover patterns and relationships in data that may be used to make accurate predictions and decisions. Statistical inference concerns the same problems. Are the two really different? Through a series of case studies, we will try to illuminate some of the challenges of data mining and highlight some of the differences between data mining and traditional statistical analysis. Each case study reminds us that the important issues are often the ones that transcend the methodological choice one faces when solving real world problems.
