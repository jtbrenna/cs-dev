Nonlinear interactions between certain subsets of components in complex interconnected systems can cause qualitatively dramatic changes in emergent system properties.  For example, certain genes and environmental triggers can interact to cause disease, some subsets of molecules will catalyze their own interactions and may be key to the emergence of Life, and some initially localized market interactions can ripple into collapse of entire economies. However, given the vast numbers of components and interactions in real-world systems, how does one go about determining which small subsets of components are likely to trigger large discrete changes in system properties?  In this talk, I will frame this type of combinatorial search problem as nonlinear feature selection, and will describe an efficient stochastic algorithm that can be used to tackle this problem in a variety of application domains.  As a specific case study, I will focus on recent research in which we seek to find combinations of outages that lead to large cascading failures in the electrical grid, so that we can mine the results for predictive patterns. Our results show scale-free distributions in the likelihood that network outages interact in deleterious combinations, indicating that a relatively small number of electrical components contribute disproportionately to cascading failure risk. Standard grid performance indices and common metrics of network topology are shown to be insufficient for predicting which outages are likely to result in cascading failure, underscoring the value in using our approach to identify system vulnerabilities. 

