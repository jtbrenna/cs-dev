<h3>CS Research Day Guest Speaker</h3>

<h4 class="subtitle">Cognition inspired by the human brain: Novel way of building new generation of dynamic
systems</h4>

<p>


<strong>Dr. Simon Haykin, B.Sc., Ph.D., D.Sc. F.R.S.C., F.I.E.E.E.</strong></p>

<p>
<strong>December 10, 2010<br />
</strong>9:00-10:00 a.m.<br />
Perkins 101</p>

<p><strong>Abstract</strong><br />
I will  begin my lecture by describing four processes that are basic to human cognition: 
<ul><li>The perception-action cycle</li>
<li>Memory</li>
<li>Attention</li>
<li>Intelligence</li></ul>
Then, I will describe my current work on Cognitive Radar and show how the visual brain has influenced my way of thinking about radar, and do so by presenting experimental results that are ground-breaking.

</p>

<p><strong>Speaker Bio</strong>
<br />
Simon Haykin received his B.Sc. (First-class Honours), Ph.D., and D.Sc., all in Electrical Engineering from the University of Birmingham, England. He is a Fellow of the Royal Society of Canada, and a Fellow of the Institute of Electrical and Electronics Engineers. He is the recipient of the Henry Booker Gold Medal from URSI, 2002, the Honorary Degree of Doctor of Technical Sciences from ETH Zentrum, Zurich, Switzerland, 1999, and many other medals and prizes. He is a pioneer in adaptive signal-processing with emphasis on applications in radar and communications, an area of research which has occupied much of his professional life. In the mid 1980s, he shifted the thrust of his research effort in the direction of Neural Computation, which was re-emerging at that time. All along, he had the vision of revisiting the ?elds of radar and communications from a brand new perspective. That vision became a reality in the early years of this century with the publication of two seminal journal papers:
<ul><li>"Cognitive Radio: Brain-empowered Wireless communications", which appeared in IEEE J. Selected Areas in Communications, Feb. 2005</li>
<li>"Cognitive Radar: A Way of the Future", which appeared in the IEEE J. Signal Processing, Feb. 2006</li></ul>

<p>Cognitive Radio and Cognitive Radar are two important parts of a much wider and multidisciplinary subject: Cognitive Dynamic Systems, research into which has become his passion.</p>
