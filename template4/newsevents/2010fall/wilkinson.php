M.S. in Digital Investigation Management, Champlain College
</p>
<p>
<strong>Thursday, October 28<br />
</strong>6:30-8:30 p.m.<br />
105 Votey Hall</p>

<p><strong>Summary</strong><br />
Just as the use of technology to assist in everyday activities has increased
significantly in the past decade, the use of the same technology by criminals has also increased. This talk will explore the role evidence sourced from digital devices plays in criminal investigations. It will also address the common misconception that digital evidence is only relevant to computer related crime. A series of case studies will examine how digital evidence has assisted in the successful prosecution of offences including fraud, murder and serious assaults.

</p>

<p><strong>Speaker Bio</strong>
<br />

Michael Wilkinson is the Program Director of the Master of Science in Digital
Investigation Management at Champlain College. Prior to joining Champlain he was a Coordinator with the State Electronic Evidence Branch (SEEB) of the New South Wales Police Force. In his time with SEEB Michael examined hundreds of digital devices, developed processes for the extraction of evidence from a range of devices and presented evidence in court as an expert witness. Michael has also been involved in the development of a standard for the preservation of digital evidence with Standards Australia, taught courses in digital forensics at Macquarie University and the University of South Australia and developed national competencies for digital forensic examiners with the Australian National Institute of Forensic Science.
</p>


