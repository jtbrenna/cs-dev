<h4 class="subtitle">"Exploring Revenue Concentration Within The Firm Using an Agent-based Simulation
"</h4>

<p><strong>Keith B. Hermiz, Ph.D.</strong><br>

<p><span class="red">CANCELLED DUE TO WEATHER</span></p>

<p><strong>February 24, 2010<br />
</strong>
12:50 - 1:40 pm<br>
 367 Votey Hall</p>

<p><strong>Summary</strong>
<br>
Many, if not most, firms experience some degree of revenue concentration across their customer base.  Extraordinarily high levels of revenue attributed to a small number of customers can be viewed as a risk to the ongoing viability of the firm and requires appropriate warnings in financial audits.  The question arises, is revenue concentration a natural consequence of a firm's operating environment or is it driven by certain behaviors that might reflect the firm's marketing strategy?  In the latter case, if one were to understand the dynamics that lead to increased revenue concentration it could potentially be mitigated.  While revenue concentration across firms has been widely studied, revenue concentration within firms has received far less attention, if any at all.  In this paper we will use a simple buyer-seller agent simulation to explore the emergence of revenue concentration in the business-to-business environment.  Options to mitigate concentration and the resulting effects on total revenue are discussed.
</p>
<p><strong>Speaker Bio</strong>
<br>
Dr. Hermiz manages the Marketing Science Practice within IBM's Market Insights Center of Excellence.  He has three decades of industry experience in information technology and analytic methods.  He is co-inventor on four patents in the application of data mining to intrusion detection and marketing.  He has held a variety of consulting leadership positions within IBM, worked for major consulting firms, managed his own services delivery company and taught at the university level. He currently chairs the cross-organizational Analytic Practitioner Community within IBM.  He holds a Ph.D. in Applied Mathematics from the University of Maryland, College Park and a MBA from the University of Rhode Island
