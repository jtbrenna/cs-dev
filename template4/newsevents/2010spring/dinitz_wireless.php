<h3>Seminar Description</h3>

<h4 class="subtitle">"Approximating Wireless Capacity in the Physical Model"</h4>

<p><a href="http://www.cs.cmu.edu/~mdinitz/" target="_blank"><strong>Michael Dinitz</strong></a><br>
Computer Science Dept., Carnegie Mellon University</p>

<p><strong>January 20, 2010</strong><br>
12:50 - 1:40 pm<br>
367 Votey Hall</p>

<p><strong>Summary</strong>
<br>
This talk considers the problem of maximizing the number of supported connections in arbitrary wireless networks, where a transmission is supported if and only if the signal-to-interference-plus-noise ratio at the receiver is greater than some threshold.  The aim is to choose transmission powers for each connection so as to maximize the number of connections for which this threshold is met. I believe that analyzing this problem is important both in its own right and also because it arises as a subproblem in many other areas of wireless networking. This problem is also intriguing since it involves both continuous aspects (i.e., choosing the transmission powers) and discrete aspects (i.e., which connections should be supported).
</p>
<p>
In this talk I will give approximation algorithms for this problem with approximation ratios independent of the number of connections.  I will also discuss some related game theory, showing that for a natural game played by the transmitters the price of anarchy is also independent of the number of connections. Finally, I will show how the techniques used to prove the price of anarchy can be extended to design-distributed algorithms for the problem.
</p>
<p>
This talk is partially based on joint work with Matthew Andrews (Bell Labs).
</p>
<p><strong>Speaker Bio</strong>
<br>
Michael Dinitz is a Ph.D. student in the Computer Science Department at Carnegie Mellon University, where he works on algorithms for problems related to computer networking.  He is also interested more generally in approximation algorithms for hard optimization problems. He is the recipient of a National Science Foundation (NSF) Graduate Research Fellowship and has been a research intern at Alcatel-Lucent Bell Laboratories and Microsoft Research Silicon Valley. Prior to his doctoral studies he graduated summa cum laude from Princeton University with a degree in computer science and a minor in applied and computational mathematics.
</p>

