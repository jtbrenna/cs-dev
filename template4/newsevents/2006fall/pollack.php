<p><strong>Summary</strong>
<br />
For many years my lab has been working on electronic and software systems which can learn and develop on their own in open-ended innovative ways. This is based on understanding and mimicking natural co-evolution. However, in nature, co-evolution refers to the contingent development between species. For machine learning, co-evolution has come to mean the search for &quot;arms-race&quot; type phenomena which can lead multiple agents to develop through their own interaction, without the need for an intelligent designer.</p>
<p> The setup is usually as a set of players to a &quot;game&quot; who start with only the rules and must develop strategy or tactics through interaction. Generally, this interaction is a two-level competition &#151; first in playing a game, and second in competing for limited slots in a fixed-size population. We have had some success, for example in optimization, such as discovering the best sorting networks and cellular automata rules, as well as in three generations of automatically designed robots. However, as we developed these co-evolutionary learning algorithms, we found many phenomena which arise to prevent continuous innovation. These phenomena are economic rather than biologic, and include winner-take-all monopolies, boom/bust cycles, and stable mediocre oligarchies (groups of players who tacitly collude to protect each other from further innovation.)</p>
<p>We have been developing new techniques for avoiding these problems in co-evolution, including Pareto co-evolution, emergent dimensions, and memory mechanisms. We have developed a new principle to understand learning among agents &#151; &quot;The Teacher’s Dilemma,&quot; which models the teacher-student relationship, and provides a new interaction framework which is neither competitive nor altruistic. The first major practical application of this work has been the development of scaleable peer-to-peer learning environments for children. These are multi-player online video games, but the highest scores accrue to players who provide appropriate challenges to each other, essentially turning students into each other’s teachers. We launched the first online spelling bee in 2004 and now have 40,000 members in <a href="http://www.Beeweb.org">www.Beeweb.org</a>.</p>
