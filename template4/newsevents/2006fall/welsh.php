<p><strong>Summary</strong>
<br />
Wireless sensor networks are an exciting new technology with many potential   applications in the natural sciences. Our group has been collaborating with   seismologists from UNH, UNC, and the Instituto Geofisico in Ecuador to   explore the use of sensor networks for&nbsp;&nbsp;   monitoring eruptions and earthquakes at active volcanoes. The use of   wireless sensor networks can augment and supplant existing seismic monitoring   equipment, which often involves heavy, power-hungry data loggers.&nbsp;&nbsp; This is an especially challenging   application, requiring high-resolution signal collection across an array of   spatially-separated sensors to understand the geophysical processes underlying   volcanic activity.&nbsp; In this talk, I will   describe two sensor network deployments that we have undertaken on volcanoes in   Ecuador, Tungurahua and Reventador,   in the summers of 2004 and 2005. The Reventador network consisted of 16 wireless   sensor nodes, distributed over a 3 km aperture, that   collected high-resolution seismic and acoustic data on over 200 eruptions and   earthquakes over 3 weeks. This project involved many challenges,&nbsp;&nbsp; including reliable multihop routing,   fine-grained network time synchronization, over-the-air reprogramming, and   event-based triggering. I will also discuss the lessons learned from deploying a   sensor network in such a hostile and remote location. Reaching the deployment   site required slogging through dense jungle for several hours to the upper   flanks of the volcano, deforested by a massive eruption in 2002.</p>

<p><strong>Speaker Bio</strong>
<br />
Matt Welsh is an   assistant professor of Computer Science at Harvard University. His research interests span   many aspects of complex systems, including Internet services, distributed   systems, and sensor networks. His current projects include macroprogramming   languages and resource management techniques for sensor networks, as well as   deployments in application settings such as medical care and seismology. He is   also a long-time Linux hacker and is the author of &quot;Running Linux&quot;, published by   O'Reilly and Associates.</p>

