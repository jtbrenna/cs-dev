<p><strong>Summary</strong>
<br />
This is a presentation of our earlier paper written with Anju Dahiya and Jaweed Asraf and published in International Journal of General System. We propose a new General System Theory Framework which uses the concept &lsquo;model&rsquo; from predicate calculus for the description of systems states and behavior. The framework emerged as an effort to formalize the concept of emergent organization that constitutes the foundation of the Science of Complexity. The framework provides a natural expression for such concepts as &lsquo;organization&rsquo;, &lsquo;structure&rsquo;, &lsquo;behavior&rsquo;, &lsquo;pattern of organization&rsquo;, and&nbsp; &lsquo;emergence&rsquo;.&nbsp; We found that the classical concept, isomorphism of models has a natural interpretation as isomorphism of systems that the models represent. Besides the theoretical analysis of fundamental concepts of General Systems Theory, the framework designed could be used for applications in ALife research. The framework was developed as a part of our ecomodeling project. A full fledged multiagent simulator LEM , implemented in Visual Prolog is an example of practical application of LMSD.</p>
